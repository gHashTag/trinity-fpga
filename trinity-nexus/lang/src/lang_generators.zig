    const w = result.writer(allocator);

    try w.print("// {s} v{s}\n", .{ spec.name, spec.version });
    try w.print("// Generated by VIBEE Multi-Language Generator (MGEN-003 Fluent TypeScript)\n", .{});
    try w.print("// φ² + 1/φ² = 3\n\n", .{});

    // MGEN-003: ESM module format with utility types
    try w.print("// Type utilities\n", .{});
    try w.print("export type Maybe<T> = T | null;\n", .{});
    try w.print("export type Nullable<T> = T | null | undefined;\n\n", .{});

    // MGEN-003: Custom error class
    try w.print("/** Custom error for {s} module */\n", .{spec.name});
    try w.print("export class {s}Error extends Error {{\n", .{spec.name});
    try w.print("  constructor(message: string, public readonly code?: string) {{\n", .{});
