// ═══════════════════════════════════════════════════════════════════════════════

pub fn generateGo(allocator: Allocator, spec: ParsedSpec) ![]u8 {
    var result: std.ArrayListUnmanaged(u8) = .empty;
    const w = result.writer(allocator);

    try w.print("// {s} v{s}\n", .{ spec.name, spec.version });
    try w.print("// Generated by VIBEE Multi-Language Generator (MGEN-004 Fluent Go)\n", .{});
    try w.print("// φ² + 1/φ² = 3\n\n", .{});
    try w.print("package {s}\n\n", .{spec.name});

    // MGEN-004: Import fmt for error wrapping
    try w.print("import (\n", .{});
    try w.print("\t\"fmt\"\n", .{});
    try w.print(")\n\n", .{});

    // MGEN-004: Custom error type
    try w.print("// {s}Error represents errors in {s}\n", .{ spec.name, spec.name });
    try w.print("type {s}Error struct {{\n", .{toPascalCase(spec.name)});
    try w.print("\tCode    string\n", .{});
    try w.print("\tMessage string\n", .{});
    try w.print("\tErr     error\n", .{});
    try w.print("}}\n\n", .{});

    try w.print("func (e *{s}Error) Error() string {{\n", .{toPascalCase(spec.name)});
    try w.print("\tif e.Err != nil {{\n", .{});
    try w.print("\t\treturn fmt.Sprintf(\"%s: %s: %v\", e.Code, e.Message, e.Err)\n", .{});
    try w.print("\t}}\n", .{});
    try w.print("\treturn fmt.Sprintf(\"%s: %s\", e.Code, e.Message)\n", .{});
    try w.print("}}\n\n", .{});

    try w.print("func (e *{s}Error) Unwrap() error {{\n", .{toPascalCase(spec.name)});
    try w.print("\treturn e.Err\n", .{});
    try w.print("}}\n\n", .{});

    // Generate types with constructor
    for (spec.types) |t| {
        try w.print("// {s} represents a {s} entity\n", .{t.name, t.name});
        try w.print("type {s} struct {{\n", .{t.name});
        for (t.fields) |f| {
            const go_type = mapTypeGoFluent(f.type_name);
            try w.print("\t{c}{s} {s} `json:\"{s}\"`\n", .{ std.ascii.toUpper(f.name[0]), f.name[1..], go_type, f.name });
        }
        try w.print("}}\n\n", .{});

        // MGEN-004: Add constructor
        try w.print("// New{s} creates a new {s} with required fields\n", .{t.name, t.name});
        try w.print("func New{s}(", .{t.name});

        var first = true;
        for (t.fields) |f| {
            if (!isOptionalTypeGo(f.type_name)) {
                if (!first) try w.print(", ", .{});
                // Use lowerCamel for parameter name
                try w.print("{c}{s}", .{ std.ascii.toLower(f.name[0]), f.name[1..] });
                try w.print(" {s}", .{mapTypeGoFluent(f.type_name)});
                first = false;
            }
        }

        try w.print(") *{s} {{\n", .{t.name});
        try w.print("\treturn &{s}{{\n", .{t.name});

        for (t.fields) |f| {
            if (isOptionalTypeGo(f.type_name)) {
                try w.print("\t\t{c}{s}: nil,\n", .{ std.ascii.toUpper(f.name[0]), f.name[1..] });
            } else {
                try w.print("\t\t{c}{s}: {c}{s},\n", .{ std.ascii.toUpper(f.name[0]), f.name[1..], std.ascii.toLower(f.name[0]), f.name[1..] });
            }
        }

        try w.print("\t}}\n", .{});
        try w.print("}}\n\n", .{});
    }

    // Generate behaviors with error wrapping
    for (spec.behaviors) |b| {
        if (b.implementation.len > 0) {
            const impl = std.mem.trim(u8, b.implementation, &std.ascii.whitespace);
            try w.print("// {s}: Given {s}, When {s}, Then {s}\n", .{ b.name, b.given, b.when, b.then });
            try w.print("{s}\n\n", .{impl});
        } else {
            // MGEN-004: Generate with error return
            try w.print("// {s}: Given {s}, When {s}, Then {s}\n", .{ b.name, b.given, b.when, b.then });
            try w.print("func {s}(", .{toPascalCase(b.name)});

            // Add context parameter if implied
            const has_ctx = std.mem.indexOf(u8, b.given, "context") != null or
                           std.mem.indexOf(u8, b.when, "concurrent") != null;
            if (has_ctx) {
                try w.print("ctx context.Context", .{});
            }

            try w.print(") error {{\n", .{});
            try w.print("\treturn &{s}Error{{\n", .{toPascalCase(spec.name)});
            try w.print("\t\tCode:    \"NOT_IMPLEMENTED\",\n", .{});
            try w.print("\t\tMessage: \"{s} not implemented\",\n", .{b.name});
            try w.print("\t}}\n", .{});
            try w.print("}}\n\n", .{});
        }
    }

    return result.toOwnedSlice(allocator);
}

// MGEN-004: Enhanced type mapping with proper Go types
