// PYTHON GENERATOR (MGEN-001: Fluent Python)
// ═══════════════════════════════════════════════════════════════════════════════

pub fn generatePython(allocator: Allocator, spec: ParsedSpec) ![]u8 {
    var result: std.ArrayListUnmanaged(u8) = .empty;
    const w = result.writer(allocator);

    try w.print("# {s} v{s}\n", .{ spec.name, spec.version });
    try w.print("# Generated by VIBEE Multi-Language Generator (MGEN-001 Fluent Python)\n", .{});
    try w.print("# φ² + 1/φ² = 3\n\n", .{});

    // MGEN-001: Enhanced imports for fluent Python
    try w.print("from __future__ import annotations\n", .{});
    try w.print("from dataclasses import dataclass, field\n", .{});
    try w.print("from typing import Optional, List, Dict, Any, TypeVar, Generic, Callable, Awaitable\n", .{});
    try w.print("from contextlib import contextmanager\n", .{});
    try w.print("from abc import ABC, abstractmethod\n", .{});
    try w.print("import asyncio\n\n", .{});

    // Custom exception for the module
    try w.print("class {s}Error(Exception):\n", .{spec.name});
    try w.print("    \"\"\"Base exception for {s}\"\"\"\n", .{spec.name});
    try w.print("    pass\n\n", .{});

    // Generate types with enhanced dataclass patterns
    for (spec.types) |t| {
        try w.print("@dataclass\n", .{});
        try w.print("class {s}:\n", .{t.name});
        try w.print("    \"\"\"Auto-generated from .vibee spec\"\"\"\n", .{});

        if (t.fields.len == 0) {
            try w.print("    pass\n", .{});
        } else {
            for (t.fields) |f| {
                const py_type = mapTypePythonFluent(f.type_name);
                const default_val = if (isOptionalType(f.type_name)) " = None" else "";
                try w.print("    {s}: {s}{s}\n", .{ f.name, py_type, default_val });
            }
        }

        // MGEN-001: Add __post_init__ for validation
        try w.print("\n", .{});
        try w.print("    def __post_init__(self) -> None:\n", .{});
        try w.print("        \"\"\"Validate fields after initialization\"\"\"\n", .{});
        try w.print("        pass\n", .{});

        // MGEN-001: Add __repr__ for better debugging
        try w.print("\n", .{});
        try w.print("    def __repr__(self) -> str:\n", .{});
        try w.print("        fields = ', '.join(f'{{k}}={{v!r}}' for k, v in self.__dict__.items())\n", .{});
        try w.print("        return f'{s}({{fields}})'\n", .{spec.name});

        try w.print("\n\n", .{});
    }

    // Generate behaviors with enhanced patterns
    for (spec.behaviors) |b| {
        const is_async = std.mem.indexOf(u8, b.given, "async") != null or
                         std.mem.indexOf(u8, b.when, "async") != null;
        const is_generator = std.mem.indexOf(u8, b.name, "iter") != null or
                           std.mem.indexOf(u8, b.name, "stream") != null;
        const is_context_manager = std.mem.indexOf(u8, b.name, "with") != null;

        if (b.implementation.len > 0) {
            const impl = std.mem.trim(u8, b.implementation, &std.ascii.whitespace);
            try w.print("{s}\n\n", .{impl});
        } else {
            // Generate fluent docstring
            try w.print("def {s}{s}:\n", .{ b.name, if (is_async) "()" else "" });
            try w.print("    \"\"\"\n", .{});
            try w.print("    Given: {s}\n", .{b.given});
            try w.print("    When:  {s}\n", .{b.when});
            try w.print("    Then:  {s}\n", .{b.then});
            try w.print("    \"\"\"\n", .{});

            if (is_async) {
                try w.print("    await asyncio.sleep(0)  # Placeholder for async operation\n", .{});
            } else if (is_generator) {
                try w.print("    yield  # Generator pattern\n", .{});
            } else if (is_context_manager) {
                try w.print("    yield  # Context manager pattern\n", .{});
            } else {
                try w.print("    raise {s}Error(\"Not implemented\")\n", .{spec.name});
            }
            try w.print("\n\n", .{});
        }
    }

    // MGEN-001: Add async context manager helper
    try w.print("@contextmanager\n", .{});
    try w.print("async def {s}_context():\n", .{spec.name});
    try w.print("    \"\"\"Async context manager for {s} operations\"\"\"\n", .{spec.name});
    try w.print("    yield {s}()\n\n", .{spec.name});

    return result.toOwnedSlice(allocator);
}

// MGEN-001: Enhanced type mapping with proper generics
fn mapTypePythonFluent(vibee_type: []const u8) []const u8 {
    if (std.mem.eql(u8, vibee_type, "String")) return "str";
    if (std.mem.eql(u8, vibee_type, "Int")) return "int";
    if (std.mem.eql(u8, vibee_type, "Float")) return "float";
    if (std.mem.eql(u8, vibee_type, "Bool")) return "bool";

    // Parse generic types like List<String> → List[str]
    if (std.mem.startsWith(u8, vibee_type, "List<")) {
        const inner = vibee_type[5 .. vibee_type.len - 1];
        const inner_mapped = mapTypePythonFluent(inner);
        if (std.mem.eql(u8, inner_mapped, "str")) return "List[str]";
        if (std.mem.eql(u8, inner_mapped, "int")) return "List[int]";
        return "List[Any]";
    }

    if (std.mem.startsWith(u8, vibee_type, "Option<")) {
        const inner = vibee_type[8 .. vibee_type.len - 1];
        const inner_mapped = mapTypePythonFluent(inner);
        if (std.mem.eql(u8, inner_mapped, "str")) return "str | None";
        if (std.mem.eql(u8, inner_mapped, "int")) return "int | None";
        return "Any | None";
    }

    return "Any";
}

fn isOptionalType(vibee_type: []const u8) bool {
    return std.mem.startsWith(u8, vibee_type, "Option<");
}

