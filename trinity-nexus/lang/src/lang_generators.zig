            try w.print("  /** {s} field */\n", .{f.name});
            try w.print("  readonly {s}{s}: {s};\n", .{ f.name, optional, ts_type });
        }
        try w.print("}}\n\n", .{});

        // MGEN-003: Add type guard
        try w.print("/** Type guard for {s} */\n", .{t.name});
        try w.print("export function is{s}(value: unknown): value is {s} {{\n", .{ t.name, t.name });
        try w.print("  return (\n", .{});
        try w.print("    typeof value === 'object' &&\n", .{});
        try w.print("    value !== null &&\n", .{});
