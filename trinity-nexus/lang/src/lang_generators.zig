// ═══════════════════════════════════════════════════════════════════════════════

pub fn generateRust(allocator: Allocator, spec: ParsedSpec) ![]u8 {
    var result: std.ArrayListUnmanaged(u8) = .empty;
    const w = result.writer(allocator);

    try w.print("// {s} v{s}\n", .{ spec.name, spec.version });
    try w.print("// Generated by VIBEE Multi-Language Generator (MGEN-002 Fluent Rust)\n", .{});
    try w.print("// φ² + 1/φ² = 3\n\n", .{});

    // MGEN-002: Enhanced imports for idiomatic Rust
    try w.print("use serde::{{Serialize, Deserialize}};\n", .{});
    try w.print("use thiserror::Error;\n\n", .{});

    // MGEN-002: Custom error type for the module
    try w.print("#[derive(Debug, Error)]\n", .{});
    try w.print("pub enum {s}Error {{\n", .{spec.name});
    try w.print("    #[error(\"Invalid operation: {{0}}\")]\n", .{});
    try w.print("    InvalidOperation(String),\n", .{});
    try w.print("    #[error(\"Not found: {{0}}\")]\n", .{});
    try w.print("    NotFound(String),\n", .{});
    try w.print("    #[error(\"Validation failed: {{0}}\")]\n", .{});
    try w.print("    Validation(String),\n", .{});
    try w.print("    #[error(\"IO error: {{0}}\")]\n", .{});
    try w.print("    Io(#[from] std::io::Error),\n", .{});
    try w.print("}}\n\n", .{});

    // MGEN-002: Result type alias
    try w.print("pub type {s}Result<T> = Result<T, {s}Error>;\n\n", .{ spec.name, spec.name });

    // Generate types with enhanced derives
    for (spec.types) |t| {
        // MGEN-002: Enhanced derive macros
        try w.print("#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq)]\n", .{});

        // Check if any field is optional to add Default derive
        const has_optional = for (t.fields) |f| {
            if (isOptionalTypeRust(f.type_name)) break true;
        } else false;
        if (has_optional or t.fields.len == 0) {
            try w.print("#[derive(Default)]\n", .{});
        }

        try w.print("pub struct {s} {{\n", .{t.name});
        for (t.fields) |f| {
            const rs_type = mapTypeRustFluent(f.type_name);
            const default_val = if (isOptionalTypeRust(f.type_name)) " = None" else "";
            try w.print("    pub {s}: {s}{s},\n", .{ f.name, rs_type, default_val });
        }
        try w.print("}}\n\n", .{});

        // MGEN-002: Add impl block with builder pattern and new()
        try w.print("impl {s} {{\n", .{t.name});
        try w.print("    /// Create a new instance with all required fields\n", .{});

        // Build constructor parameters
        if (t.fields.len > 0) {
            try w.print("    pub fn new(", .{});
            var first = true;
            for (t.fields) |f| {
                if (!isOptionalTypeRust(f.type_name)) {
                    if (!first) try w.print(", ", .{});
                    try w.print("{s}: {s}", .{ f.name, mapTypeRustFluent(f.type_name) });
                    first = false;
                }
            }
            try w.print(") -> Self {{\n", .{});
            try w.print("        Self {{\n", .{});
            for (t.fields) |f| {
                if (isOptionalTypeRust(f.type_name)) {
                    try w.print("            {s}: None,\n", .{f.name});
                } else {
                    try w.print("            {s},\n", .{f.name});
                }
            }
            try w.print("        }}\n", .{});
            try w.print("    }}\n", .{});
            try w.print("}}\n\n", .{});
        } else {
            try w.print("    pub fn new() -> Self {{\n", .{});
            try w.print("        Self {{}}\n", .{});
            try w.print("    }}\n", .{});
            try w.print("}}\n\n", .{});
        }
    }

    // Generate behaviors with Result types
    for (spec.behaviors) |b| {
        if (b.implementation.len > 0) {
            // Use custom implementation (contains full function definition)
            const impl = std.mem.trim(u8, b.implementation, &std.ascii.whitespace);
            try w.print("/// Given: {s}, When: {s}, Then: {s}\n", .{ b.given, b.when, b.then });
            try w.print("{s}\n\n", .{impl});
        } else {
            // MGEN-002: Generate stub with Result type
            try w.print("/// Given: {s}, When: {s}, Then: {s}\n", .{ b.given, b.when, b.then });

            // Detect async behavior
            const is_async = std.mem.indexOf(u8, b.given, "async") != null or
                             std.mem.indexOf(u8, b.when, "async") != null;
            const is_iterator = std.mem.indexOf(u8, b.name, "iter") != null or
                                std.mem.indexOf(u8, b.name, "stream") != null;

            if (is_async) {
                try w.print("pub async fn {s}() -> {s}Result<()> {{\n", .{ b.name, spec.name });
            } else if (is_iterator) {
                try w.print("pub fn {s}() -> impl Iterator<Item = {s}Result<String>> {{\n", .{ b.name, spec.name });
                try w.print("    std::iter::empty()\n", .{});
            } else {
                try w.print("pub fn {s}() -> {s}Result<()> {{\n", .{ b.name, spec.name });
            }
            try w.print("    todo!()\n", .{});
            try w.print("}}\n\n", .{});
        }
    }

    return result.toOwnedSlice(allocator);
}

