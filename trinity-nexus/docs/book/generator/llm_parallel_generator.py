def save_chapter(num: int, content: str):
    """Сохраняет главу в файл"""
    том, книга, _ = coords(num)
    тома = ["ⲧⲟⲙ_1_ⲙⲉⲇⲛⲟⲉ", "ⲧⲟⲙ_2_ⲥⲉⲣⲉⲃⲣⲟ", "ⲧⲟⲙ_3_ⲍⲟⲗⲟⲧⲟ"]
