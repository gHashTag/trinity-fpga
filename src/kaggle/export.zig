// BATCH EXPORTER — Export all tracks
// ═══════════════════════════════════════════════════════════════════════════════

pub const BatchExporter = struct {
    allocator: Allocator,
    output_dir: []const u8 = "kaggle/submissions",

    pub fn init(allocator: Allocator) BatchExporter {
        return .{
            .allocator = allocator,
        };
    }

    pub fn exportAll(self: *const BatchExporter, results: std.StringHashMap([]const SubmissionRow)) !void {
        // Create output directory
        std.fs.cwd().makePath(self.output_dir) catch {};

        var iter = results.iterator();
        while (iter.next()) |entry| {
            const track = entry.key_ptr.*;
            const rows = entry.value_ptr.*;

            const output_path = try std.fmt.allocPrint(self.allocator, "{s}/{s}_submission.csv", .{ self.output_dir, track });
            defer self.allocator.free(output_path);

            const exporter = Exporter.init(self.allocator);
            try exporter.exportCsv(rows, output_path);
        }
    }
};

