test "export CSV" {
    const allocator = std.testing.allocator;
    const exporter = Exporter.init(allocator);

    const rows = [_]SubmissionRow{
        .{ .id = "test_001", .answer = "A" },
        .{ .id = "test_002", .answer = "B" },
    };

    const tmp_path = "test_export_temp.csv";
    {
        try exporter.exportCsv(&rows, tmp_path);
    }
    defer std.fs.cwd().deleteFile(tmp_path) catch {};

    // Verify output
    const content = try std.fs.cwd().readFileAlloc(allocator, tmp_path, 1024);
    defer allocator.free(content);

    try std.testing.expect(std.mem.indexOf(u8, content, "id,answer") != null);
    try std.testing.expect(std.mem.indexOf(u8, content, "test_001") != null);
}

