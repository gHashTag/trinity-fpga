/// Main entry point for `tri forge fpga` command
pub fn runFpgaCommand(allocator: std.mem.Allocator, args: []const []const u8) !void {
    if (args.len < 1) {
        try printSacredConstHelp();
        return;
    }
    const subcommand = args[0];
    const sub_args = if (args.len > 1) args[1..] else &[_][]const u8{};

    if (std.mem.eql(u8, subcommand, "phi-arith")) {
        try runPhiArith(allocator, sub_args);
    } else if (std.mem.eql(u8, subcommand, "cordic-sacred")) {
        try runCordicSacred(allocator, sub_args);
    } else if (std.mem.eql(u8, subcommand, "const-rom")) {
        try runConstRom(allocator, sub_args);
    } else if (std.mem.eql(u8, subcommand, "synth-report")) {
        try runSynthReport(allocator, sub_args);
    } else if (std.mem.eql(u8, subcommand, "vsa-bind")) {
        try runVsaBind(allocator, sub_args);
    } else {
        std.debug.print("{s}Error:{s} Unknown subcommand: {s}\n\n", .{ RED, RESET, subcommand });
        try printSacredConstHelp();
    }
}

