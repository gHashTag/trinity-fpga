fn generateConstRomVerilog(allocator: std.mem.Allocator, target: []const u8, depth: u32) ![]const u8 {
    _ = target;

    const template =
        \\// ============================================================================
        \\// SACRED CONSTANTS ROM — Continued Fraction Convergents
        \\// ============================================================================
        \\//
        \\// Target: φ²/π² = 0.161552...
        \\// CF = [0; 6, 4, 1, 8, 8, 1, 3, ...]
        \\//
        \\// Each ROM entry stores (numerator, denominator) of CF convergent
        \\//
        \\// Generated by TRI sacred-const const-rom
        \\//
        \\// ============================================================================
        \\
        \\`default_nettype none
        \\
        \\module sacred_const_rom #(
        \\    parameter DEPTH = {d},
        \\    parameter WIDTH = 50,   // 25 bits numerator + 25 bits denominator
        \\    parameter ADDR_WIDTH = 8
        \\)(
        \\    input  wire clk,
        \\    input  wire [ADDR_WIDTH-1:0] addr,
        \\    output wire [24:0] numerator,
        \\    output wire [24:0] denominator,
        \\    output wire valid
        \\);
        \\
        \\    // ROM storage: convergents of φ²/π²
        \\    reg [24:0] rom_num [0:DEPTH-1];
        \\    reg [24:0] rom_den [0:DEPTH-1];
        \\
        \\    // Initialize ROM with CF convergents
        \\    integer i;
        \\    initial begin
        \\        // Core convergents (repeat pattern for larger depths)
        \\        rom_num[0] = 25'd0;
        \\        rom_den[0] = 25'd1;
        \\        rom_num[1] = 25'd1;
        \\        rom_den[1] = 25'd6;
        \\        rom_num[2] = 25'd4;
        \\        rom_den[2] = 25'd25;
        \\        rom_num[3] = 25'd5;
        \\        rom_den[3] = 25'd31;
        \\        rom_num[4] = 25'd44;
        \\        rom_den[4] = 25'd273;
        \\        rom_num[5] = 25'd351;
        \\        rom_den[5] = 25'd2183;
        \\        rom_num[6] = 25'd395;
        \\        rom_den[6] = 25'd2456;
        \\        rom_num[7] = 25'd1131;
        \\        rom_den[7] = 25'd7017;
        \\
        \\        // Fill remaining entries with pattern repetition
        \\        for (i = 8; i < DEPTH; i = i + 1) begin
        \\            rom_num[i] = rom_num[i % 8];
        \\            rom_den[i] = rom_den[i % 8];
        \\        end
        \\    end
        \\
        \\    // Registered read output
        \\    reg [24:0] num_reg, den_reg;
        \\    reg valid_reg;
        \\
        \\    always @(posedge clk) begin
        \\        num_reg <= rom_num[addr];
        \\        den_reg <= rom_den[addr];
        \\        valid_reg <= 1'b1;
        \\    end
        \\
        \\    assign numerator = num_reg;
        \\    assign denominator = den_reg;
        \\    assign valid = valid_reg;
        \\
        \\endmodule
        \\
        \\`default_nettype wire
    ;

    return std.fmt.allocPrint(allocator, template, .{depth});
}

