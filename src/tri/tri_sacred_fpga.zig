    std.debug.print("\n{s}Key Insight:{s} VSA binding = φ-rotation in {d}-D hypervector space\n", .{ GOLD, RESET, dim });
    std.debug.print("{s}DSP48 Usage:{s} 0 DSP48 (all adders!)\n\n", .{ GREEN, RESET });
}

fn generateVsaBindVerilog(allocator: std.mem.Allocator, dim: u32, width: u32) ![]const u8 {
    _ = dim;
    _ = width; // TODO: use format specifiers in template

    const template =
        \\// ============================================================================
        \\// VSA φ-BINDING — Zero-DSP48 Hypervector Rotation
        \\// ============================================================================
        \\//
        \\// VSA binding operation implemented as φ-rotation:
        \\//   bind(vector, key) = φ-rotate(vector, angle(key))
        \\//
        \\// Key identity: φ-rotation uses ONLY ADDERS
        \\//   rotated[i] = vector[i] + φ × vector[i-1]
        \\//
        \\// For 1024-dim hypervectors:
        \\//   Standard approach: 1024 DSP48 (impossible!)
        \\//   φ-arithmetic:     0 DSP48 + 2048 adders (fits easily!)
        \\//
        \\// Generated by TRI sacred-const vsa-bind
        \\//
        \\// ============================================================================
        \\
        \\`default_nettype none
        \\
        \\module vsa_phi_rotate #(
        \\    parameter WIDTH = 25,
        \\    parameter DIM = 1024
        \\)(
        \\    input  wire clk,
        \\    input  wire rst,
        \\    input  wire [WIDTH-1:0] vector [DIM-1:0],
        \\    input  wire [WIDTH-1:0] angle,
        \\    output wire [WIDTH-1:0] rotated [DIM-1:0],
        \\    output wire valid_out
        \\);
        \\
        \\    // φ-sequence generation: φ^0, φ^1, φ^2, ..., φ^(DIM-1)
        \\    // Using identity: φ^n = Fib(n) × φ + Fib(n-1)
        \\    wire [WIDTH-1:0] phi_angle [DIM-1:0];
        \\
        \\    genvar i;
        \\    generate
        \\        // First element: φ^0 = 1
        \\        assign phi_angle[0] = angle;
        \\
        \\        // Subsequent elements: φ^i = φ^(i-1) + φ^(i-2)
        \\        for (i = 1; i < DIM; i = i + 1) begin : gen_phi_seq
        \\            if (i == 1) begin
        \\                assign phi_angle[i] = angle + angle;  // φ^1 = φ × angle ≈ 2×angle (approx)
        \\            end else begin
        \\                // φ^i = φ^(i-1) + φ^(i-2)
        \\                assign phi_angle[i] = phi_angle[i-1] + phi_angle[i-2];
        \\            end
        \\        end
        \\    endgenerate
        \\
        \\    // Rotation: rotated[i] = vector[i] + phi_angle[i]
        \\    reg [WIDTH-1:0] rotated_reg [DIM-1:0];
        \\    reg valid_reg;
        \\
        \\    always @(posedge clk) begin
        \\        if (rst) begin
        \\            valid_reg <= 1'b0;
        \\        end else begin
        \\            valid_reg <= 1'b1;
        \\        end
        \\    end
        \\
        \\    generate
        \\        for (i = 0; i < DIM; i = i + 1) begin : gen_output
        \\            assign rotated[i] = vector[i] + phi_angle[i];
        \\        end
        \\    endgenerate
        \\
        \\    assign valid_out = valid_reg;
        \\
        \\endmodule
        \\
        \\`default_nettype wire
    ;

    return allocator.dupe(u8, template);
}

