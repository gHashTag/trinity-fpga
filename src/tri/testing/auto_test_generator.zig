test "AutoTestGenerator generates code" {
    const allocator = std.testing.allocator;
    var registry = try CommandRegistry.init(allocator);
    defer registry.deinit();

    // Add a test command
    try registry.commands.append(.{
        .name = "test_cmd",
        .category = .math,
        .priority = .high,
        .example_args = &[_][]const u8{"arg1"},
        .expected_patterns = &[_][]const u8{"result"},
        .description = "Test command",
    });

    const options = TestGenOptions{
        .include_sacred_assertions = true,
        .verbose = false,
    };

    var generator = AutoTestGenerator.init(allocator, &registry, options);
    const code = try generator.generateAll();
    defer allocator.free(code);

    // Should contain test code
    try std.testing.expect(code.len > 0);
    try std.testing.expect(std.mem.indexOf(u8, code, "test \"") != null);
}

