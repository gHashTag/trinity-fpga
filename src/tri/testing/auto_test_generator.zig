/// Quick generation with default options
pub fn generateTests(allocator: std.mem.Allocator, registry: *CommandRegistry) ![]const u8 {
    const options = TestGenOptions{
        .include_sacred_assertions = true,
        .verbose = false,
    };

    var generator = AutoTestGenerator.init(allocator, registry, options);
    return generator.generateAll();
}

