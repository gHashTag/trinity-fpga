test "emit_zig: generateZigFromBytecode produces valid Zig code" {
    const allocator = std.testing.allocator;

    const test_bytecode = [_]u8{
        0x00, 0x00, 0x00, 0x00, // NOP
        0xFF, 0x00, 0x00, 0x00, // HALT
        0x03, 0x08, 0x10, 0x00, // ADD r1, r2
        0x80, 0x00, 0x00, 0x00, // PHI_CONST r0
    };

    const result = try generateZigFromBytecode(allocator, &test_bytecode);
    defer allocator.free(result);

    // Verify it generates valid Zig code
    try std.testing.expect(std.mem.startsWith(u8, result, "// TRI-27 VM"));
    try std.testing.expect(std.mem.indexOf(u8, result, "pub const CPUState") != null);
    try std.testing.expect(std.mem.indexOf(u8, result, "pub fn execute") != null);
    try std.testing.expect(std.mem.indexOf(u8, result, "phi_const") != null);
    try std.testing.expect(std.mem.indexOf(u8, result, "pi_const") != null);
    try std.testing.expect(std.mem.indexOf(u8, result, "e_const") != null);

    print("{s}✅ emit_zig.zig generates valid Zig code{s}\n", .{ GREEN, RESET });
}

test "emit_zig: contains all sacred constants" {
    const allocator = std.testing.allocator;
    const empty_bytecode = [_]u8{ 0x00, 0x00, 0x00, 0x00 };

    const result = try generateZigFromBytecode(allocator, &empty_bytecode);
