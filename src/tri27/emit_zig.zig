// Generate Zig code that implements TRI‑27 VM
pub fn generateZigFromBytecode(allocator: Allocator, bytecode: []const u8) ![]const u8 {
    if (bytecode.len % 4 != 0) {
        print("{s}Error: Bytecode must be multiple of 4 bytes{s}\n", .{ RED, RESET });
        return error.InvalidBytecode;
    }

    var generated = std.ArrayList(u8).initCapacity(allocator, 4096) catch return error.OutOfMemory;
    defer generated.deinit(allocator);

    // Module header
    try generated.appendSlice(allocator,
        \\// TRI-27 VM — Auto-generated from TRI‑27 bytecode
        \\// Generated: 2026-03-24
        \\// DO NOT EDIT - This file is generated by emit_zig.zig
        \\
        \\const std = @import("std");
        \\const Allocator = std.mem.Allocator;
        \\
        \\pub const Trit27 = i3; // {-1, 0, +1}
        \\
        \\pub const CPUState = struct {
        \\    // 27 ternary registers
        \\    t: [27]i32 = [_]i32{0} ** 27,
        \\    // Alias: r[i] = t[i]
        \\    // 3 sacred constant registers (φ, π, e) in GF16 format
        \\    phi_const: i32 = 0x3F9E3779,  // φ = 1.61803398875
        \\    pi_const: i32 = 0x408F4197,   // π = 3.14159265359
        \\    e_const: i32 = 0x402DF854,    // e = 2.718281828
        \\
        \\    // Vector registers (8 GF16 + 3 float + 8 T-word)
        \\    v_gf16: [8]f32 = [_]f32{0.0} ** 8,
        \\    v_float: [3]f32 = [_]f32{0.0} ** 3,
        \\    v_tword: [8][27]Trit27 = [_][27]Trit27{.{0} ** 27} ** 8,
        \\
        \\    // Stack (16-word return address stack)
        \\    stack: [16]i32 = [_]i32{0} ** 16,
        \\    sp: u4 = 0,
        \\
        \\    // Program counter
        \\    pc: u16 = 0,
        \\
        \\    // Status flags
        \\    flags: u8 = 0,
        \\
        \\    // Memory (256 words × 32-bit)
        \\    memory: [256]i32 = [_]i32{0} ** 256,
        \\
        \\    // Flag bits
        \\    inline fn flagH(self: *const CPUState) bool { return (self.flags & (1 << 0)) != 0; }
        \\    inline fn flagZ(self: *const CPUState) bool { return (self.flags & (1 << 6)) != 0; }
        \\    inline fn flagN(self: *const CPUState) bool { return (self.flags & (1 << 7)) != 0; }
        \\    inline fn flagV(self: *const CPUState) bool { return (self.flags & (1 << 2)) != 0; }
        \\    inline fn flagC(self: *const CPUState) bool { return (self.flags & (1 << 5)) != 0; }
        \\
        \\    pub fn init() CPUState {
        \\        return .{};
        \\    }
        \\
        \\    pub fn reset(self: *CPUState) void {
        \\        self.* = CPUState.init();
        \\    }
        \\
        \\    pub fn loadBytecode(self: *CPUState, bytecode: []const u8) !void {
        \\        if (bytecode.len % 4 != 0) return error.InvalidBytecode;
        \\        const word_count = bytecode.len / 4;
        \\        for (0..@min(word_count, 256)) |i| {
        \\            const b0 = bytecode[i * 4 + 0];
        \\            const b1 = bytecode[i * 4 + 1];
        \\            const b2 = bytecode[i * 4 + 2];
        \\            const b3 = bytecode[i * 4 + 3];
        \\            self.memory[i] = @as(i32, b0) << 24 | @as(i32, b1) << 16 | @as(i32, b2) << 8 | b3;
        \\        }
        \\    }
        \\};
        \\
        \\pub const ExecuteResult = enum {
        \\    halted,
        \\    timeout,
        \\    invalid,
        \\};
        \\
        \\// ALU Operations
        \\pub fn aluAdd(a: i32, b: i32) struct { result: i32, flags: u8 } {
        \\    const result = a + b;
        \\    var flags: u8 = 0;
        \\    if (result == 0) flags |= 1 << 6;
        \\    if (result < 0) flags |= 1 << 7;
        \\    // Overflow detection
        \\    if (((a ^ result) & (b ^ result)) < 0) flags |= 1 << 2;
        \\    return .{ .result = result, .flags = flags };
        \\}
        \\
        \\pub fn aluSub(a: i32, b: i32) struct { result: i32, flags: u8 } {
        \\    const result = a - b;
        \\    var flags: u8 = 0;
        \\    if (result == 0) flags |= 1 << 6;
        \\    if (result < 0) flags |= 1 << 7;
        \\    if (((a ^ b) & (a ^ result)) < 0) flags |= 1 << 2;
        \\    return .{ .result = result, .flags = flags };
        \\}
        \\
        \\// Ternary multiplication: {-1, 0, +1} * {-1, 0, +1}
        \\pub fn aluMul(a: i32, b: i32) i32 {
        \\    // Standard multiplication, but trits have special properties
        \\    return a * b;
        \\}
        \\
        \\pub fn aluDiv(a: i32, b: i32) !i32 {
        \\    if (b == 0) return error.DivisionByZero;
        \\    return @divTrunc(a, b);
        \\}
        \\
        \\pub fn aluAnd(a: i32, b: i32) i32 { return a & b; }
        \\pub fn aluOr(a: i32, b: i32) i32 { return a | b; }
        \\pub fn aluXor(a: i32, b: i32) i32 { return a ^ b; }
        \\pub fn aluShl(a: i32, count: u5) i32 { return @shlExact(a, count); }
        \\
        \\// Stack operations
        \\pub fn stackPush(cpu: *CPUState, value: i32) !void {
        \\    if (cpu.sp >= 15) return error.StackOverflow;
        \\    cpu.stack[cpu.sp] = value;
        \\    cpu.sp += 1;
        \\}
        \\
        \\pub fn stackPop(cpu: *CPUState) !i32 {
        \\    if (cpu.sp == 0) return error.StackUnderflow;
        \\    cpu.sp -= 1;
        \\    return cpu.stack[cpu.sp];
        \\}
        \\
        \\// Sacred constant loaders
        \\pub fn loadPhi(dst: *i32) void {
        \\    dst.* = 0x3F9E3779; // φ in GF16 format
        \\}
        \\
        \\pub fn loadPi(dst: *i32) void {
        \\    dst.* = 0x408F4197; // π in GF16 format
        \\}
        \\
        \\pub fn loadE(dst: *i32) void {
        \\    dst.* = 0x402DF854; // e in GF16 format
        \\}
        \\
        \\// Vector dot product (8×8 GF16)
        \\pub fn vectorDotProduct(a: []const f32, b: []const f32) f32 {
        \\    var sum: f32 = 0.0;
        \\    for (0..@min(a.len, b.len)) |i| {
        \\        sum += a[i] * b[i];
        \\    }
        \\    return sum;
        \\}
        \\
        \\// Main execution function
        \\pub fn execute(cpu: *CPUState, max_cycles: u32) ExecuteResult {
        \\    var cycles: u32 = 0;
        \\
        \\    while (cycles < max_cycles) : (cycles += 1) {
        \\        if (cpu.pc >= 256) return .invalid;
        \\
        \\        // Fetch instruction word
        \\        const instr_word = cpu.memory[cpu.pc];
        \\        const opcode_byte: u8 = @truncate(@as(u32, @bitCast(instr_word)) >> 24);
        \\        const opcode_val: Opcode = @enumFromInt(opcode_byte);
        \\
        \\        // Decode operands
        \\        const dst: u5 = @truncate(@as(u32, @bitCast(instr_word)) >> 20);
        \\        const src1: u5 = @truncate(@as(u32, @bitCast(instr_word)) >> 15);
        \\        const src2: u5 = @truncate(@as(u32, @bitCast(instr_word)) >> 10);
        \\        const imm: i16 = @bitCast(@as(u16, @truncate(@as(u32, @bitCast(instr_word)))));
        \\
        \\        cpu.pc += 1;
        \\
        \\        // Execute opcode
        \\        switch (opcode_val) {
        \\            .NOP => {},
        \\            .HALT => { cpu.flags |= 1 << 0; return .halted; },
        \\            .LD => {
        \\                const addr = @as(usize, @intCast(cpu.t[src1]));
        \\                if (addr < 256) cpu.t[dst] = cpu.memory[addr];
        \\            },
        \\            .ST => {
        \\                const addr = @as(usize, @intCast(cpu.t[src1]));
        \\                if (addr < 256) cpu.memory[addr] = cpu.t[dst];
        \\            },
        \\            .ADD => {
        \\                const res = aluAdd(cpu.t[src1], cpu.t[src2]);
        \\                cpu.t[dst] = res.result;
        \\                cpu.flags = res.flags;
        \\            },
        \\            .SUB => {
        \\                const res = aluSub(cpu.t[src1], cpu.t[src2]);
        \\                cpu.t[dst] = res.result;
        \\                cpu.flags = res.flags;
        \\            },
        \\            .MUL => {
        \\                cpu.t[dst] = aluMul(cpu.t[src1], cpu.t[src2]);
        \\            },
        \\            .DIV => {
        \\                cpu.t[dst] = aluDiv(cpu.t[src1], cpu.t[src2]) catch 0;
        \\            },
        \\            .AND => {
        \\                cpu.t[dst] = aluAnd(cpu.t[src1], cpu.t[src2]);
        \\            },
        \\            .OR => {
        \\                cpu.t[dst] = aluOr(cpu.t[src1], cpu.t[src2]);
        \\            },
        \\            .XOR => {
        \\                cpu.t[dst] = aluXor(cpu.t[src1], cpu.t[src2]);
        \\            },
        \\            .SHL => {
        \\                const shift: u5 = @truncate(@as(u32, @bitCast(cpu.t[src2])));
        \\                cpu.t[dst] = aluShl(cpu.t[src1], shift);
        \\            },
        \\            .LDI => {
        \\                cpu.t[dst] = @as(i32, imm);
        \\            },
        \\            .STI => {
        \\                const addr = @as(usize, @intCast(@as(i32, imm)));
        \\                if (addr < 256) cpu.memory[addr] = cpu.t[src1];
        \\            },
        \\            .LTI => {
        \\                const addr = @as(usize, @intCast(cpu.t[src1]));
        \\                const offset = @as(usize, @intCast(@as(i32, imm)));
        \\                if (addr + offset < 256) cpu.t[dst] = cpu.memory[addr + offset];
        \\            },
        \\            .INC => {
        \\                cpu.t[dst] = cpu.t[src1] + 1;
        \\            },
        \\            .DEC => {
        \\                cpu.t[dst] = cpu.t[src1] - 1;
        \\            },
        \\            .JMP => {
        \\                cpu.pc = @as(u16, @bitCast(@as(i16, imm)));
        \\            },
        \\            .JZ => {
        \\                if (cpu.t[dst] == 0) {
        \\                    cpu.pc = @as(u16, @bitCast(@as(i16, imm)));
        \\                }
        \\            },
        \\            .JNZ => {
        \\                if (cpu.t[dst] != 0) {
        \\                    cpu.pc = @as(u16, @bitCast(@as(i16, imm)));
        \\                }
        \\            },
        \\            .MOV => {
        \\                cpu.t[dst] = cpu.t[src1];
        \\            },
        \\            .PUSH => {
        \\                stackPush(cpu, cpu.t[src1]) catch {};
        \\            },
        \\            .POP => {
        \\                cpu.t[dst] = stackPop(cpu) catch 0;
        \\            },
        \\            .PHI_CONST => loadPhi(&cpu.t[dst]),
        \\            .PI_CONST => loadPi(&cpu.t[dst]),
        \\            .E_CONST => loadE(&cpu.t[dst]),
        \\            .SACR => {
        \\                // Sacred operation: apply φ-based transformation
        \\                const phi_val: f32 = 1.61803398875;
        \\                cpu.t[dst] = @intFromFloat(@as(f32, @floatFromInt(cpu.t[src1])) * phi_val);
        \\            },
        \\            .DOT => {
        \\                // Vector dot product
        \\                const idx1: usize = @intCast(dst);
        \\                const idx2: usize = @intCast(src1);
        \\                if (idx1 < 8 and idx2 < 8) {
        \\                    const vec_a = cpu.v_gf16[0..8];
        \\                    const vec_b = cpu.v_gf16[0..8];
        \\                    const result = vectorDotProduct(vec_a, vec_b);
        \\                    cpu.t[dst] = @intFromFloat(result);
        \\                }
        \\            },
        \\            .RESET => {
        \\                cpu.reset();
        \\                return .halted;
        \\            },
        \\            else => return .invalid,
        \\        }
        \\    }
        \\
        \\    return .timeout;
        \\}
        \\
        \\// Run bytecode with default settings
        \\pub fn runBytecode(bytecode: []const u8) !void {
        \\    var cpu = CPUState.init();
        \\    try cpu.loadBytecode(bytecode);
        \\    const result = execute(&cpu, 100000);
        \\
        \\    const stdout = std.io.getStdOut().writer();
        \\    try stdout.print("Execution result: {}\n", .{result});
        \\    try stdout.print("Final PC: {}\n", .{cpu.pc});
        \\    try stdout.print("Flags: 0x{X:0>2}\n", .{cpu.flags});
        \\
        \\    // Print registers
        \\    try stdout.print("Registers:\n", .{});
        \\    for (0..27) |i| {
        \\        if (i % 9 == 0) try stdout.print("  ", .{});
        \\        try stdout.print("t{}={} ", .{ i, cpu.t[i] });
        \\        if (i % 9 == 8) try stdout.print("\n", .{});
        \\    }
        \\}
        \\
    );

    return generated.toOwnedSlice(allocator);
}

