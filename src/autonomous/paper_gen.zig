    }

    /// Post to X (Twitter)
    pub fn postToX(self: *PaperPublisher, message: []const u8) !void {
        _ = self;
        _ = message;
        // TODO: Implement X API integration
        // Requires API key and secret
        std.log.warn("X posting not yet implemented", .{});
    }

    /// Full publish workflow
    pub fn publishImprovement(self: *PaperPublisher, result: BenchmarkResult) !void {
        if (self.shouldPublish(result)) {
            const post = try self.generateBlogPost(result);
            defer post.deinit(self.allocator);

            try self.deployToDocsite(post);

            // Generate social media message
            const social_msg = try std.fmt.allocPrint(
                self.allocator,
                "🚀 TRINITY Auto-Improvement: +{d:.1}% on {s}!",
                .{ result.improvement_percent, result.metric_name }
            );
            defer self.allocator.free(social_msg);

            try self.postToX(social_msg);
        }
    }

    /// Generate improvement report
    pub fn generateReport(self: *PaperPublisher, results: []const BenchmarkResult) ![]const u8 {
        _ = self;

        var report = std.ArrayList(u8).init(self.allocator);
        const writer = report.writer();

        try writer.writeAll("# TRINITY Improvement Report\n\n");
        try writer.writeAll("| Metric | Improvement | Commit |\n");
        try writer.writeAll("|--------|-------------|--------|\n");

        for (results) |r| {
            try writer.print("| {s} | +{d:.1}% | {s} |\n", .{
                r.metric_name,
                r.improvement_percent,
                r.commit_hash[0..8],
            });
        }

        return report.toOwnedSlice();
    }
};

// CLI for testing
pub fn main() !void {
    const allocator = std.heap.page_allocator;

    const args = try std.process.argsAlloc(allocator);
    defer std.process.argsFree(allocator, args);

    std.debug.print("📝 TRINITY AUTONOMOUS PAPER PUBLISHING\n", .{});
    std.debug.print("φ² + 1/φ² = 3\n\n", .{});

    if (args.len < 2) {
        std.debug.print(
            \\Usage: paper_gen <command> [args]
            \\
            \\Commands:
            \\  generate <improvement> <metric>  - Generate blog post
            \\  report <baseline> <new> <metric>  - Generate report
            \\
            \\Example:
            \\  paper_gen generate 12.5 bind_speed
            \\  paper_gen report 100.0 112.5 throughput
            \\
        , .{});
        return;
    }

    const command = args[1];

    if (std.mem.eql(u8, command, "generate")) {
        if (args.len < 4) {
            std.debug.print("Usage: paper_gen generate <improvement> <metric>\n", .{});
            return;
        }

        const improvement = try std.fmt.parseFloat(f64, args[2]);
        const metric_name = args[3];

        var publisher = PaperPublisher.init(
            allocator,
            "/Users/playra/trinity-w1/docsite",
            "/Users/playra/trinity-w1"
        );

        const result = BenchmarkResult{
            .baseline_metric = 100.0,
            .new_metric = 100.0 * (1.0 + improvement / 100.0),
            .improvement_percent = improvement,
            .metric_name = metric_name,
            .commit_hash = "c40b16605",
            .timestamp = std.time.timestamp(),
        };

        const post = try publisher.generateBlogPost(result);
        defer post.deinit(allocator);

        std.debug.print("\n=== Generated Blog Post ===\n", .{});
        std.debug.print("Title: {s}\n", .{post.title});
        std.debug.print("Slug: {s}\n", .{post.slug});
        std.debug.print("\n{s}\n", .{post.content});

        if (publisher.shouldPublish(result)) {
            std.debug.print("\n✅ Meets publish threshold (+10%%)\n", .{});
        } else {
            std.debug.print("\n❌ Below publish threshold\n", .{});
        }
    } else {
        std.debug.print("Unknown command: {s}\n", .{command});
    }
}
