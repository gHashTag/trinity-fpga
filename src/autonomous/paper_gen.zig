const std = @import("std");

pub const BenchmarkResult = struct {
    baseline_metric: f64,
    new_metric: f64,
    improvement_percent: f64,
    metric_name: []const u8,
    commit_hash: []const u8,
    timestamp: i64,

    pub fn format(self: BenchmarkResult, allocator: std.mem.Allocator) ![]const u8 {
        return std.fmt.allocPrint(allocator, "{s}: {d:.2} → {d:.2} (+{d:.1}%)", .{
            self.metric_name,
            self.baseline_metric,
            self.new_metric,
            self.improvement_percent,
        });
    }
};

pub const BlogPost = struct {
    title: []const u8,
    slug: []const u8,
    content: []const u8,
    tags: []const []const u8,
    category: []const u8,
    author: []const u8,
    date: []const u8,

    pub fn deinit(self: *BlogPost, allocator: std.mem.Allocator) void {
        allocator.free(self.title);
        allocator.free(self.slug);
        allocator.free(self.content);
        allocator.free(self.category);
        allocator.free(self.author);
        allocator.free(self.date);
        for (self.tags) |tag| {
            allocator.free(tag);
        }
        allocator.free(self.tags);
    }
};

pub const PaperPublisher = struct {
    allocator: std.mem.Allocator,
    docsite_path: []const u8,
    project_root: []const u8,

    pub fn init(allocator: std.mem.Allocator, docsite_path: []const u8, project_root: []const u8) PaperPublisher {
        return .{
            .allocator = allocator,
            .docsite_path = docsite_path,
            .project_root = project_root,
        };
    }

    /// Check if improvement meets publish threshold
    pub fn shouldPublish(self: *PaperPublisher, result: BenchmarkResult) bool {
        _ = self;
        return result.improvement_percent >= 10.0; // +10% threshold
    }

    /// Generate slug from title
    fn generateSlug(self: *PaperPublisher, title: []const u8) ![]const u8 {
        _ = self;

        // Simple slug generation: lowercase, replace spaces with hyphens
        const slug = try self.allocator.alloc(u8, title.len);
        var slug_idx: usize = 0;
        for (title) |c| {
            if (std.ascii.isAlNum(c)) {
                slug[slug_idx] = std.ascii.toLower(c);
                slug_idx += 1;
            } else if (c == ' ') {
                slug[slug_idx] = '-';
                slug_idx += 1;
            }
        }
        return slug[0..slug_idx];
    }

    /// Generate blog post from benchmark results
    pub fn generateBlogPost(self: *PaperPublisher, result: BenchmarkResult) !BlogPost {
        const date = try self.allocator.dupe(u8, "2026-03-06"); // TODO: actual date
        defer self.allocator.free(date);

        const title = try std.fmt.allocPrint(
            self.allocator,
            "Auto-Improvement: {d:.1}% on {s}",
            .{ result.improvement_percent, result.metric_name }
        );

        const slug = try self.generateSlug(title);

        // Generate markdown content
        const content = try std.fmt.allocPrint(
            self.allocator,
            \\# {s}
            \\
            \\**RALPH Autonomous Improvement**
            \\
            \\Trinity's self-improving AI agent has achieved a **{d:.1}% improvement** on `{s}` performance.
            \\
            \\## Results
            \\
            \\| Metric | Before | After | Improvement |
            \\|--------|--------|-------|------------|
            \\| {s} | {d:.2} | {d:.2} | +{d:.1}% |
            \\
            \\## Technical Details
            \\
            \\- **Commit**: `{s}`
            \\- **Timestamp**: {d}
            \\- **Improvement Type**: Autonomous optimization
            \\
            \\This improvement was automatically discovered and implemented by RALPH (Trinity's Orchestrator).
            \\
            \\φ² + 1/φ² = 3
            \\
            \\---
            \\
            \\*Auto-generated by TRINITY SELF-EVOLUTION SYSTEM*
        , .{
            title,
            result.improvement_percent,
            result.metric_name,
            result.metric_name,
            result.baseline_metric,
            result.new_metric,
            result.improvement_percent,
            result.commit_hash,
            result.timestamp,
        });

        // Tags
        const tags = try self.allocator.alloc([]const u8, 3);
        tags[0] = try self.allocator.dupe(u8, "auto-improvement");
        tags[1] = try self.allocator.dupe(u8, "benchmark");
        tags[2] = try self.allocator.dupe(u8, "trinity");

        return BlogPost{
            .title = title,
            .slug = slug,
            .content = content,
            .tags = tags,
            .category = try self.allocator.dupe(u8, "Research"),
            .author = try self.allocator.dupe(u8, "RALPH AI"),
            .date = date,
        };
    }

    /// Write blog post to docsite
    fn writeBlogPost(self: *PaperPublisher, post: BlogPost) !void {
        const research_dir = try std.fmt.allocPrint(
            self.allocator,
            "{s}/docs/research/auto-generated",
            .{self.docsite_path}
        );
        defer self.allocator.free(research_dir);

        // Create directory
        std.fs.cwd().makePath(research_dir) catch |err| {
            if (err != error.PathAlreadyExists) {
                return err;
            }
        };

        // Write markdown file
        const file_path = try std.fmt.allocPrint(
            self.allocator,
            "{s}/{s}.md",
            .{ research_dir, post.slug }
        );
        defer self.allocator.free(file_path);

        const file = try std.fs.cwd().createFile(file_path, .{});
        defer file.close();

        try file.writeAll(post.content);
    }

    /// Deploy blog post to docsite
    pub fn deployToDocsite(self: *PaperPublisher, post: BlogPost) !void {
        // Write blog post
        try self.writeBlogPost(post);

