test "PendingPattern: meets threshold" {
    const pattern = PendingPattern{
        .template = "test pattern",
        .fix_type = .SYNTAX_FIX,
        .confidence = 0.95,
        .sample_count = 15,
        .created_at = 0,
        .embedding = undefined,
    };

    try std.testing.expect(pattern.meetsThreshold(0.9, 10));
}

test "PendingPattern: below threshold" {
    const pattern = PendingPattern{
        .template = "test pattern",
        .fix_type = .SYNTAX_FIX,
        .confidence = 0.5,
        .sample_count = 5,
        .created_at = 0,
        .embedding = undefined,
    };

    try std.testing.expect(!pattern.meetsThreshold(0.9, 10));
}

test "SelfModification: initialization" {
    const allocator = std.testing.allocator;
    var sm = try SelfModification.init(allocator, "/tmp/test.zig");
    defer sm.deinit();

    try std.testing.expectEqual(@as(f64, 0.9), sm.confidence_threshold);
    try std.testing.expectEqual(@as(usize, 10), sm.min_sample_count);
}

test "SelfModification: propose pattern below threshold" {
    const allocator = std.testing.allocator;
    var sm = try SelfModification.init(allocator, "/tmp/test.zig");
    defer sm.deinit();

    const ready = try sm.proposePattern("test pattern", .SYNTAX_FIX, 0.5, 5);

    try std.testing.expect(!ready); // Below threshold
    try std.testing.expectEqual(@as(usize, 1), sm.pending_patterns.items.len);
}

test "SelfModification: propose pattern above threshold" {
    const allocator = std.testing.allocator;
    var sm = try SelfModification.init(allocator, "/tmp/test.zig");
    defer sm.deinit();

    const ready = try sm.proposePattern("high conf pattern", .TYPE_FIX, 0.95, 15);

    try std.testing.expect(ready); // Above threshold
    try std.testing.expectEqual(@as(usize, 1), sm.pending_patterns.items.len);
}

test "SelfModification: ready count" {
    const allocator = std.testing.allocator;
    var sm = try SelfModification.init(allocator, "/tmp/test.zig");
    defer sm.deinit();

    _ = try sm.proposePattern("low conf", .SYNTAX_FIX, 0.5, 5);
    _ = try sm.proposePattern("high conf", .TYPE_FIX, 0.95, 15);

    try std.testing.expectEqual(@as(usize, 1), sm.readyCount());
}

test "SelfModification: merge pattern" {
    const allocator = std.testing.allocator;
    var sm = try SelfModification.init(allocator, "/tmp/test.zig");
    defer sm.deinit();

    // Add initial pattern
    _ = try sm.proposePattern("merge test", .TYPE_FIX, 0.8, 10);

    // Merge with additional samples
    _ = try sm.mergePattern("merge test", .TYPE_FIX, 0.9, 10);

    // Should have 20 total samples, blended confidence
    try std.testing.expectEqual(@as(usize, 1), sm.pending_patterns.items.len);
    try std.testing.expectEqual(@as(usize, 20), sm.pending_patterns.items[0].sample_count);
}

test "SelfModification: generate mod code" {
    const allocator = std.testing.allocator;
    var sm = try SelfModification.init(allocator, "/tmp/test.zig");
    defer sm.deinit();

    _ = try sm.proposePattern("expected ';'", .SYNTAX_FIX, 0.95, 15);

    const code = try sm.generateModCode();
    defer allocator.free(code);

    try std.testing.expect(std.mem.indexOf(u8, code, "AUTO-GENERATED PATTERNS") != null);
    try std.testing.expect(std.mem.indexOf(u8, code, "expected ';'") != null);
}

test "generateTestPatterns" {
    const allocator = std.testing.allocator;
    const patterns = try generateTestPatterns(allocator);
    defer {
        for (patterns) |p| allocator.free(p.template);
        allocator.free(patterns);
    }

    try std.testing.expectEqual(@as(usize, 5), patterns.len);
}
