const std = @import("std");
const Allocator = std.mem.Allocator;

pub const TypeDef = struct {
    name: []const u8,
    fields: []const Field,
};

pub const Field = struct {
    name: []const u8,
    type_name: []const u8,
};

pub const Behavior = struct {
    name: []const u8,
    given: []const u8,
    when: []const u8,
    then: []const u8,
    implementation: []const u8 = "",
};

pub const ParsedSpec = struct {
    name: []const u8,
    version: []const u8,
    types: []const TypeDef,
    behaviors: []const Behavior,
};

// ═══════════════════════════════════════════════════════════════════════════════
// PYTHON GENERATOR (Fluent v2 - Dataclasses with Type Hints)
// ═══════════════════════════════════════════════════════════════════════════════

pub fn generatePython(allocator: Allocator, spec: ParsedSpec) ![]u8 {
    var result: std.ArrayListUnmanaged(u8) = .empty;
    const w = result.writer(allocator);

    try w.print("# {s} v{s}\n", .{ spec.name, spec.version });
    try w.print("# Generated by VIBEE Multi-Language Generator (Fluent Python v2)\n", .{});
    try w.print("# φ² + 1/φ² = 3\n\n", .{});
    try w.print("\"\"\"{s} module - Fluent Python generated from VIBEE spec.\n\nThis module uses idiomatic Python patterns:\n- dataclasses for clean structure\n- Generic type hints (PEP 585)\n- __repr__ and __eq__ for debugging\n- Class-level docstrings\n\"\"\"\n\n", .{spec.name});
    try w.print("from __future__ import annotations\n", .{});
    try w.print("from dataclasses import dataclass, field\n", .{});
    try w.print("from typing import Optional, List, Dict, Any, Generic, TypeVar\n", .{});
    try w.print("from enum import Enum\n\n", .{});

    // Generate enums first if any
    var has_enums = false;
    for (spec.types) |t| {
        if (std.mem.endsWith(u8, t.name, "Kind") or std.mem.endsWith(u8, t.name, "Type")) {
            if (!has_enums) {
                try w.print("# ==============\n", .{});
                try w.print("# Enums\n", .{});
                try w.print("# ==============\n\n", .{});
                has_enums = true;
            }
            try w.print("class {s}(Enum):\n", .{t.name});
            try w.print("    \"\"\"{s} enumeration.\"\"\"\n", .{t.name});
            for (t.fields) |f| {
                // Convert to uppercase for enum constant
                var upper_name: [64]u8 = undefined;
                var upper_len: usize = 0;
                for (f.name) |c| {
                    if (c >= 'a' and c <= 'z') {
                        upper_name[upper_len] = c - 32;
                    } else {
                        upper_name[upper_len] = c;
                    }
                    upper_len += 1;
                }
                try w.print("    {s} = \"{s}\"\n", .{ upper_name[0..upper_len], f.name });
            }
            try w.print("\n", .{});
        }
    }

    try w.print("# ═══════════════════════════════════════════════════════════════════\n", .{});
    try w.print("# Dataclasses\n", .{});
    try w.print("# ═══════════════════════════════════════════════════════════════════\n\n", .{});

    for (spec.types) |t| {
        // Skip enums - already generated
        if (std.mem.endsWith(u8, t.name, "Kind") or std.mem.endsWith(u8, t.name, "Type")) continue;

        try w.print("@dataclass\n", .{});
        try w.print("class {s}:\n", .{t.name});
        try w.print("    \"\"\"", .{});
        try w.print("{s} data structure.\n\n", .{t.name});
        if (t.fields.len > 0) {
            try w.print("Attributes:\n", .{});
            for (t.fields) |f| {
                try w.print("    {s}: {s}\n", .{ f.name, mapTypePython(f.type_name) });
            }
        }
        try w.print("\"\"\"\n", .{});

        if (t.fields.len == 0) {
            try w.print("    pass\n", .{});
        } else {
            for (t.fields) |f| {
                const py_type = mapTypePythonFluent(f.type_name);
                try w.print("    {s}: {s}\n", .{ f.name, py_type });
            }
        }

        // Add factory method if has fields
        if (t.fields.len > 0) {
            try w.print("\n", .{});
            try w.print("    @classmethod\n", .{});
            try w.print("    def create(cls, **kwargs: Any) -> {s}:\n", .{t.name});
            try w.print("        \"\"\"Factory method with validation.\"\"\"\n", .{});
            try w.print("        return cls(**kwargs)\n", .{});
        }

        try w.print("\n", .{});
    }

    try w.print("# ═══════════════════════════════════════════════════════════════════\n", .{});
    try w.print("# Behaviors\n", .{});
    try w.print("# ═══════════════════════════════════════════════════════════════════\n\n", .{});

    for (spec.behaviors) |b| {
        if (b.implementation.len > 0) {
            const impl = std.mem.trim(u8, b.implementation, &std.ascii.whitespace);
            try w.print("{s}\n\n", .{impl});
        } else {
            try w.print("def {s}() -> None:\n", .{b.name});
            try w.print("    \"\"\"\n", .{});
            try w.print("    {s} behavior.\n\n", .{b.name});
            try w.print("    Given:\n", .{});
            try w.print("        {s}\n\n", .{b.given});
            try w.print("    When:\n", .{});
            try w.print("        {s}\n\n", .{b.when});
            try w.print("    Then:\n", .{});
            try w.print("        {s}\n", .{b.then});
            try w.print("    \"\"\"\n", .{});
            try w.print("    raise NotImplementedError(\"{s}: TODO - implement behavior\")\n\n", .{b.name});
        }
    }

    return result.toOwnedSlice(allocator);
}

/// Fluent Python type mapping with generics
fn mapTypePythonFluent(vibee_type: []const u8) []const u8 {
    if (std.mem.eql(u8, vibee_type, "String")) return "str";
    if (std.mem.eql(u8, vibee_type, "Int")) return "int";
    if (std.mem.eql(u8, vibee_type, "Float")) return "float";
    if (std.mem.eql(u8, vibee_type, "Bool")) return "bool";
    if (std.mem.eql(u8, vibee_type, "U8")) return "int";
    if (std.mem.eql(u8, vibee_type, "U16")) return "int";
    if (std.mem.eql(u8, vibee_type, "U32")) return "int";
    if (std.mem.eql(u8, vibee_type, "U64")) return "int";
    if (std.mem.eql(u8, vibee_type, "I8")) return "int";
    if (std.mem.eql(u8, vibee_type, "I16")) return "int";
    if (std.mem.eql(u8, vibee_type, "I32")) return "int";
    if (std.mem.eql(u8, vibee_type, "I64")) return "int";
    if (std.mem.eql(u8, vibee_type, "Usize")) return "int";
    if (std.mem.eql(u8, vibee_type, "f32")) return "float";
    if (std.mem.eql(u8, vibee_type, "f64")) return "float";

    // Generic types with proper parameter parsing
    if (std.mem.startsWith(u8, vibee_type, "List<")) {
        const inner = vibee_type[5 .. vibee_type.len - 1];
        if (inner.len > 0 and inner[0] != 0) {
            // For List<String>, List<Int>, etc.
            const inner_type = mapTypePythonFluent(inner);
            if (std.mem.eql(u8, inner_type, "Any")) {
                return "list[Any]";
            }
            // Can't do dynamic string concat in return, use list[Any]
            return "list[Any]";
        }
        return "list[Any]";
    }
    if (std.mem.startsWith(u8, vibee_type, "Option<")) {
        const inner = vibee_type[7 .. vibee_type.len - 1];
        const inner_type = mapTypePythonFluent(inner);
        if (std.mem.eql(u8, inner_type, "str")) return "str | None";
        if (std.mem.eql(u8, inner_type, "int")) return "int | None";
        if (std.mem.eql(u8, inner_type, "float")) return "float | None";
        if (std.mem.eql(u8, inner_type, "bool")) return "bool | None";
        return "Any | None";
    }
    if (std.mem.startsWith(u8, vibee_type, "Map<")) {
        return "dict[str, Any]";
    }

    return "Any";
}

/// Legacy Python type mapping (kept for compatibility)
fn mapTypePython(vibee_type: []const u8) []const u8 {
    return mapTypePythonFluent(vibee_type);
}

