pub fn generateRust(allocator: Allocator, spec: ParsedSpec) ![]u8 {
    var result: std.ArrayListUnmanaged(u8) = .empty;
    const w = result.writer(allocator);

    try w.print("// {s} v{s}\n", .{ spec.name, spec.version });
    try w.print("// Generated by VIBEE Multi-Language Generator (Fluent Rust v2)\n", .{});
    try w.print("// φ² + 1/φ² = 3\n\n", .{});
    try w.print("//! {s} module - Fluent Rust generated from VIBEE spec.\n", .{spec.name});
    try w.print("//!\n", .{});
    try w.print("//! This module uses idiomatic Rust patterns:\n", .{});
    try w.print("//! - Result<T, E> for error handling\n", .{});
    try w.print("//! - Derive traits for common behavior\n", .{});
    try w.print("//! - impl Trait for abstraction\n", .{});
    try w.print("//! - thiserror for error types\n\n", .{});

    try w.print("use serde::{{Serialize, Deserialize}};\n", .{});
    try w.print("use thiserror::Error;\n\n", .{});

    try w.print("#[derive(Debug, Error)]\n", .{});
    try w.print("pub enum {s}Error {{\n", .{spec.name});
    try w.print("    #[error(\"generic {s} error\")]\n", .{spec.name});
    try w.print("    Generic,\n", .{});
    try w.print("    #[error(\"not found\")]\n", .{});
    try w.print("    NotFound,\n", .{});
    try w.print("    #[error(\"invalid input: {{0}}\")]\n", .{});
    try w.print("    InvalidInput(String),\n", .{});
    try w.print("}}\n\n", .{});

    try w.print("// ═══════════════════════════════════════════════════════════════════\n", .{});
    try w.print("// Structs\n", .{});
    try w.print("// ═══════════════════════════════════════════════════════════════════\n\n", .{});

    for (spec.types) |t| {
        try w.print("#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]\n", .{});
        try w.print("pub struct {s} {{\n", .{t.name});
        for (t.fields) |f| {
            const rs_type = mapTypeRust(f.type_name);
            try w.print("    pub {s}: {s},\n", .{ f.name, rs_type });
        }
        try w.print("}}\n\n", .{});

        // Add impl block with constructor
        if (t.fields.len > 0) {
            try w.print("impl {s} {{\n", .{t.name});
            try w.print("    pub fn new(", .{});
            for (t.fields, 0..) |f, i| {
                try w.print("{s}: {s}", .{ f.name, mapTypeRust(f.type_name) });
                if (i < t.fields.len - 1) try w.print(", ", .{});
            }
            try w.print(") -> Self {{\n", .{});
            try w.print("        Self {{\n", .{});
            for (t.fields) |f| {
                try w.print("            {s},\n", .{f.name});
            }
            try w.print("        }}\n", .{});
            try w.print("    }}\n", .{});
            try w.print("}}\n\n", .{});
        }
    }

    try w.print("// ═══════════════════════════════════════════════════════════════════\n", .{});
    try w.print("// Behaviors\n", .{});
    try w.print("// ═══════════════════════════════════════════════════════════════════\n\n", .{});

    for (spec.behaviors) |b| {
        if (b.implementation.len > 0) {
            const impl = std.mem.trim(u8, b.implementation, &std.ascii.whitespace);
            try w.print("/// Given: {s}, When: {s}, Then: {s}\n", .{ b.given, b.when, b.then });
            try w.print("{s}\n\n", .{impl});
        } else {
            try w.print("/// Given: {s}\n", .{b.given});
            try w.print("/// When: {s}\n", .{b.when});
            try w.print("/// Then: {s}\n", .{b.then});
            try w.print("///\n", .{});
            try w.print("/// # Errors\n", .{});
            try w.print("/// Returns {s}Error if the operation fails.\n", .{spec.name});
            try w.print("pub fn {s}() -> Result<(), {s}Error> {{\n", .{ b.name, spec.name });
            try w.print("    todo!(\"implement {s}\")\n", .{b.name});
            try w.print("}}\n\n", .{});
        }
    }

    return result.toOwnedSlice(allocator);
}

fn mapTypeRust(vibee_type: []const u8) []const u8 {
    if (std.mem.eql(u8, vibee_type, "String")) return "String";
    if (std.mem.eql(u8, vibee_type, "Int")) return "i64";
    if (std.mem.eql(u8, vibee_type, "Float")) return "f64";
    if (std.mem.eql(u8, vibee_type, "Bool")) return "bool";
    if (std.mem.eql(u8, vibee_type, "U8")) return "u8";
    if (std.mem.eql(u8, vibee_type, "U16")) return "u16";
    if (std.mem.eql(u8, vibee_type, "U32")) return "u32";
    if (std.mem.eql(u8, vibee_type, "U64")) return "u64";
    if (std.mem.eql(u8, vibee_type, "I8")) return "i8";
    if (std.mem.eql(u8, vibee_type, "I16")) return "i16";
    if (std.mem.eql(u8, vibee_type, "I32")) return "i32";
    if (std.mem.eql(u8, vibee_type, "I64")) return "i64";
    if (std.mem.eql(u8, vibee_type, "Usize")) return "usize";
    if (std.mem.eql(u8, vibee_type, "f32")) return "f32";
    if (std.mem.eql(u8, vibee_type, "f64")) return "f64";
    if (std.mem.startsWith(u8, vibee_type, "List<")) return "Vec<serde_json::Value>";
