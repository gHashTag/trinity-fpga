            for (t.fields) |f| {
                try w.print("  /** {s} variant */\n", .{f.name});
                try w.print("  {s} = \"{s}\",\n", .{ f.name, f.name });
            }
            try w.print("}}\n\n", .{});
        }
    }

    try w.print("// ═══════════════════════════════════════════════════════════════════\n", .{});
    try w.print("// Interfaces\n", .{});
    try w.print("// ═══════════════════════════════════════════════════════════════════\n\n", .{});

    for (spec.types) |t| {
        // Skip enums - already generated
        if (std.mem.endsWith(u8, t.name, "Kind") or std.mem.endsWith(u8, t.name, "Type")) continue;

        try w.print("/** {s} interface */\n", .{t.name});
        try w.print("export interface {s} {{\n", .{t.name});
        for (t.fields) |f| {
            const ts_type = mapTypeTS(f.type_name);
            try w.print("  /** {s} field */\n", .{f.name});
            try w.print("  {s}: {s};\n", .{ f.name, ts_type });
        }
        try w.print("}}\n\n", .{});
    }

    try w.print("// ═══════════════════════════════════════════════════════════════════\n", .{});
    try w.print("// Functions\n", .{});
    try w.print("// ═══════════════════════════════════════════════════════════════════\n\n", .{});

    for (spec.behaviors) |b| {
        if (b.implementation.len > 0) {
            const impl = std.mem.trim(u8, b.implementation, &std.ascii.whitespace);
            try w.print("/** Given: {s}, When: {s}, Then: {s} */\n", .{ b.given, b.when, b.then });
            try w.print("{s}\n\n", .{impl});
        } else {
            try w.print("/**\n", .{});
            try w.print(" * {s} behavior\n", .{b.name});
            try w.print(" *\n", .{});
            try w.print(" * @given {s}\n", .{b.given});
            try w.print(" * @when {s}\n", .{b.when});
            try w.print(" * @then {s}\n", .{b.then});
            try w.print(" */\n", .{});
            try w.print("export function {s}(): void {{\n", .{b.name});
            try w.print("  throw new Error('{s}: TODO - implement behavior');\n", .{b.name});
            try w.print("}}\n\n", .{});
        }
    }

    // Default export
    try w.print("// ═══════════════════════════════════════════════════════════════════\n", .{});
    try w.print("// Default export\n", .{});
    try w.print("// ═══════════════════════════════════════════════════════════════════\n\n", .{});
    try w.print("export default {{\n", .{});
    for (spec.types) |t| {
        if (std.mem.endsWith(u8, t.name, "Kind") or std.mem.endsWith(u8, t.name, "Type")) continue;
        try w.print("  {s},\n", .{t.name});
    }
    for (spec.behaviors) |b| {
        try w.print("  {s},\n", .{ b.name });
    }
    try w.print("}};\n", .{});

    return result.toOwnedSlice(allocator);
}

