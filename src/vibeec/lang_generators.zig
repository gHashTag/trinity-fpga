pub fn generateTypeScript(allocator: Allocator, spec: ParsedSpec) ![]u8 {
    var result: std.ArrayListUnmanaged(u8) = .empty;
    const w = result.writer(allocator);

    try w.print("// {s} v{s}\n", .{ spec.name, spec.version });
    try w.print("// Generated by VIBEE Multi-Language Generator (Fluent TypeScript v2)\n", .{});
    try w.print("// φ² + 1/φ² = 3\n\n", .{});
    try w.print("/**\n", .{});
    try w.print(" * {s} module - Fluent TypeScript generated from VIBEE spec.\n", .{spec.name});
    try w.print(" *\n", .{});
    try w.print(" * This module uses idiomatic TypeScript patterns:\n", .{});
    try w.print(" * - ESM modules (import/export)\n", .{});
    try w.print(" * - Strict type checking\n", .{});
    try w.print(" * - Proper null handling\n", .{});
    try w.print(" * - TSDoc comments\n", .{});
    try w.print(" */\n\n", .{});

    // Generate enums first
    var has_enums = false;
    for (spec.types) |t| {
        if (std.mem.endsWith(u8, t.name, "Kind") or std.mem.endsWith(u8, t.name, "Type")) {
            if (!has_enums) {
                try w.print("// ═══════════════════════════════════════════════════════════════════\n", .{});
                try w.print("// Enums\n", .{});
                try w.print("// ═══════════════════════════════════════════════════════════════════\n\n", .{});
                has_enums = true;
            }
            try w.print("/** {s} enumeration */\n", .{t.name});
