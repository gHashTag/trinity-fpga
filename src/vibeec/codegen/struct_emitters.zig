    try builder.writeLine("    /// Create a challenge for a shard: pick byte range [offset..offset+length]");
    try builder.writeLine("    pub fn createChallenge(self: *ProofOfStorageEngine, shard_data: []const u8, offset: u32, length: u32) !PosChallenge {");
    try builder.writeLine("        if (offset + length > shard_data.len) return error.ByteRangeOutOfBounds;");
    try builder.writeLine("        self.challenges_issued += 1;");
    try builder.writeLine("        const Sha256 = std.crypto.hash.sha2.Sha256;");
    try builder.writeLine("        var cid: [32]u8 = undefined;");
    try builder.writeLine("        Sha256.hash(shard_data, &cid, .{});");
    try builder.writeLine("        var shash: [32]u8 = undefined;");
    try builder.writeLine("        Sha256.hash(shard_data, &shash, .{});");
    try builder.writeLine("        return PosChallenge{");
    try builder.writeLine("            .challenge_id = cid,");
    try builder.writeLine("            .shard_hash = shash,");
    try builder.writeLine("            .byte_offset = offset,");
    try builder.writeLine("            .byte_length = length,");
    try builder.writeLine("        };");
    try builder.writeLine("    }");
    try builder.writeLine("");
