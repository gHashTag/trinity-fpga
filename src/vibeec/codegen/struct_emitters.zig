    try builder.writeLine("    }");
    try builder.writeLine("");
    try builder.writeLine("    /// Verify a proof: recompute hash of byte range, compare to proof_hash");
    try builder.writeLine("    pub fn verify(self: *ProofOfStorageEngine, shard_data: []const u8, challenge: PosChallenge, proof: PosProof, node_id: u8) bool {");
    try builder.writeLine("        const Sha256 = std.crypto.hash.sha2.Sha256;");
    try builder.writeLine("        const slice = shard_data[challenge.byte_offset..challenge.byte_offset + challenge.byte_length];");
    try builder.writeLine("        var expected: [32]u8 = undefined;");
    try builder.writeLine("        Sha256.hash(slice, &expected, .{});");
    try builder.writeLine("        const ok = std.mem.eql(u8, &expected, &proof.proof_hash);");
    try builder.writeLine("        if (ok) {");
    try builder.writeLine("            self.challenges_passed += 1;");
    try builder.writeLine("        } else {");
    try builder.writeLine("            self.challenges_failed += 1;");
    try builder.writeLine("            if (node_id < MAX_NODES) {");
    try builder.writeLine("                self.failure_counts[node_id] += 1;");
    try builder.writeLine("                if (self.failure_counts[node_id] >= self.max_failures) {");
    try builder.writeLine("                    self.deactivated[node_id] = true;");
    try builder.writeLine("                }");
    try builder.writeLine("            }");
    try builder.writeLine("        }");
    try builder.writeLine("        return ok;");
    try builder.writeLine("    }");
    try builder.writeLine("");
    try builder.writeLine("    pub fn isDeactivated(self: *const ProofOfStorageEngine, node_id: u8) bool {");
    try builder.writeLine("        if (node_id >= MAX_NODES) return true;");
    try builder.writeLine("        return self.deactivated[node_id];");
    try builder.writeLine("    }");
    try builder.writeLine("");
    try builder.writeLine("    pub fn getFailureCount(self: *const ProofOfStorageEngine, node_id: u8) u8 {");
    try builder.writeLine("        if (node_id >= MAX_NODES) return 0;");
    try builder.writeLine("        return self.failure_counts[node_id];");
    try builder.writeLine("    }");
    try builder.writeLine("};");
    try builder.writeLine("");
}

/// Emit Kademlia DHT struct
pub fn emitDht(builder: *CodeBuilder) !void {
    try builder.writeLine("");
    try builder.writeLine("// ═══════════════════════════════════════════════════════════════════");
    try builder.writeLine("// KADEMLIA DHT — XOR Distance Routing + Global Manifest Store/Find");
    try builder.writeLine("// 256-bit node IDs, k-buckets by leading-zero-count of XOR distance.");
    try builder.writeLine("// Store shard manifests at k-closest nodes, iterative lookup.");
    try builder.writeLine("// ═══════════════════════════════════════════════════════════════════");
    try builder.writeLine("");
    try builder.writeLine("pub const DhtNodeId = [32]u8;");
    try builder.writeLine("");
    try builder.writeLine("pub const DhtPeer = struct {");
    try builder.writeLine("    id: DhtNodeId,");
    try builder.writeLine("    port: u16,");
    try builder.writeLine("    alive: bool,");
    try builder.writeLine("};");
    try builder.writeLine("");
    try builder.writeLine("pub const DhtStoreEntry = struct {");
    try builder.writeLine("    key: DhtNodeId,");
    try builder.writeLine("    value_buf: [256]u8,");
    try builder.writeLine("    value_len: u16,");
    try builder.writeLine("    stored: bool,");
    try builder.writeLine("};");
    try builder.writeLine("");
    try builder.writeLine("/// XOR distance between two 256-bit node IDs");
    try builder.writeLine("pub fn xorDistance(a: DhtNodeId, b: DhtNodeId) DhtNodeId {");
    try builder.writeLine("    var result: DhtNodeId = undefined;");
    try builder.writeLine("    for (0..32) |i| {");
    try builder.writeLine("        result[i] = a[i] ^ b[i];");
    try builder.writeLine("    }");
    try builder.writeLine("    return result;");
    try builder.writeLine("}");
    try builder.writeLine("");
    try builder.writeLine("/// Count leading zero bits in XOR distance (determines bucket index)");
    try builder.writeLine("pub fn leadingZeroBits(dist: DhtNodeId) u16 {");
    try builder.writeLine("    var count: u16 = 0;");
    try builder.writeLine("    for (0..32) |i| {");
    try builder.writeLine("        if (dist[i] == 0) {");
    try builder.writeLine("            count += 8;");
    try builder.writeLine("        } else {");
    try builder.writeLine("            count += @intCast(@clz(dist[i]));");
    try builder.writeLine("            break;");
    try builder.writeLine("        }");
    try builder.writeLine("    }");
    try builder.writeLine("    return count;");
    try builder.writeLine("}");
    try builder.writeLine("");
    try builder.writeLine("/// Compare two DhtNodeIds for ordering (used in closest-peer sort)");
    try builder.writeLine("fn distLessThan(target: DhtNodeId, a: DhtPeer, b: DhtPeer) bool {");
    try builder.writeLine("    const da = xorDistance(target, a.id);");
    try builder.writeLine("    const db = xorDistance(target, b.id);");
    try builder.writeLine("    for (0..32) |i| {");
    try builder.writeLine("        if (da[i] < db[i]) return true;");
    try builder.writeLine("        if (da[i] > db[i]) return false;");
    try builder.writeLine("    }");
    try builder.writeLine("    return false;");
    try builder.writeLine("}");
    try builder.writeLine("");
    try builder.writeLine("pub const KBucket = struct {");
    try builder.writeLine("    const K = 8; // max peers per bucket");
    try builder.writeLine("    peers: [K]DhtPeer,");
    try builder.writeLine("    count: u8,");
    try builder.writeLine("");
    try builder.writeLine("    pub fn init() KBucket {");
    try builder.writeLine("        return .{");
    try builder.writeLine("            .peers = undefined,");
    try builder.writeLine("            .count = 0,");
    try builder.writeLine("        };");
    try builder.writeLine("    }");
    try builder.writeLine("");
    try builder.writeLine("    pub fn addPeer(self: *KBucket, peer: DhtPeer) bool {");
    try builder.writeLine("        if (self.count >= K) return false;");
    try builder.writeLine("        self.peers[self.count] = peer;");
    try builder.writeLine("        self.count += 1;");
    try builder.writeLine("        return true;");
    try builder.writeLine("    }");
    try builder.writeLine("};");
    try builder.writeLine("");
    try builder.writeLine("pub const DhtEngine = struct {");
    try builder.writeLine("    const NUM_BUCKETS = 256;");
    try builder.writeLine("    const MAX_ENTRIES = 64;");
    try builder.writeLine("");
    try builder.writeLine("    self_id: DhtNodeId,");
    try builder.writeLine("    buckets: [NUM_BUCKETS]KBucket,");
    try builder.writeLine("    entries: [MAX_ENTRIES]DhtStoreEntry,");
    try builder.writeLine("    entry_count: u16,");
    try builder.writeLine("    peer_count: u16,");
    try builder.writeLine("");
    try builder.writeLine("    pub fn init(self_id: DhtNodeId) DhtEngine {");
    try builder.writeLine("        var engine: DhtEngine = undefined;");
    try builder.writeLine("        engine.self_id = self_id;");
    try builder.writeLine("        for (0..NUM_BUCKETS) |i| {");
    try builder.writeLine("            engine.buckets[i] = KBucket.init();");
    try builder.writeLine("        }");
    try builder.writeLine("        engine.entry_count = 0;");
    try builder.writeLine("        engine.peer_count = 0;");
    try builder.writeLine("        return engine;");
    try builder.writeLine("    }");
    try builder.writeLine("");
    try builder.writeLine("    /// Add a peer to the routing table in the correct k-bucket");
    try builder.writeLine("    pub fn addPeer(self: *DhtEngine, peer: DhtPeer) bool {");
    try builder.writeLine("        const dist = xorDistance(self.self_id, peer.id);");
    try builder.writeLine("        const lz = leadingZeroBits(dist);");
    try builder.writeLine("        const bucket_idx = if (lz >= NUM_BUCKETS) NUM_BUCKETS - 1 else lz;");
    try builder.writeLine("        const ok = self.buckets[bucket_idx].addPeer(peer);");
    try builder.writeLine("        if (ok) self.peer_count += 1;");
    try builder.writeLine("        return ok;");
    try builder.writeLine("    }");
    try builder.writeLine("");
    try builder.writeLine("    /// Get bucket index for a peer (by XOR distance leading zeros)");
    try builder.writeLine("    pub fn bucketFor(self: *const DhtEngine, peer_id: DhtNodeId) u16 {");
    try builder.writeLine("        const dist = xorDistance(self.self_id, peer_id);");
    try builder.writeLine("        const lz = leadingZeroBits(dist);");
    try builder.writeLine("        return if (lz >= NUM_BUCKETS) NUM_BUCKETS - 1 else lz;");
    try builder.writeLine("    }");
    try builder.writeLine("");
    try builder.writeLine("    /// Store a key-value entry");
    try builder.writeLine("    pub fn store(self: *DhtEngine, key: DhtNodeId, value: []const u8) bool {");
    try builder.writeLine("        if (self.entry_count >= MAX_ENTRIES) return false;");
    try builder.writeLine("        if (value.len > 256) return false;");
    try builder.writeLine("        var entry: DhtStoreEntry = undefined;");
    try builder.writeLine("        entry.key = key;");
    try builder.writeLine("        @memcpy(entry.value_buf[0..value.len], value);");
    try builder.writeLine("        entry.value_len = @intCast(value.len);");
    try builder.writeLine("        entry.stored = true;");
    try builder.writeLine("        self.entries[self.entry_count] = entry;");
    try builder.writeLine("        self.entry_count += 1;");
    try builder.writeLine("        return true;");
    try builder.writeLine("    }");
    try builder.writeLine("");
    try builder.writeLine("    /// Find a value by key (exact match)");
    try builder.writeLine("    pub fn find(self: *const DhtEngine, key: DhtNodeId) ?[]const u8 {");
    try builder.writeLine("        for (0..self.entry_count) |i| {");
    try builder.writeLine("            if (std.mem.eql(u8, &self.entries[i].key, &key) and self.entries[i].stored) {");
    try builder.writeLine("                return self.entries[i].value_buf[0..self.entries[i].value_len];");
    try builder.writeLine("            }");
    try builder.writeLine("        }");
    try builder.writeLine("        return null;");
    try builder.writeLine("    }");
    try builder.writeLine("");
    try builder.writeLine("    pub const ClosestResult = struct { peers: [8]DhtPeer, count: u8 };");
    try builder.writeLine("");
    try builder.writeLine("    /// Find k-closest peers to a target key");
    try builder.writeLine("    pub fn closestPeers(self: *const DhtEngine, target: DhtNodeId, k: u8) ClosestResult {");
    try builder.writeLine("        var all_peers: [256]DhtPeer = undefined;");
    try builder.writeLine("        var total: u16 = 0;");
    try builder.writeLine("        for (0..NUM_BUCKETS) |bi| {");
    try builder.writeLine("            for (0..self.buckets[bi].count) |pi| {");
    try builder.writeLine("                if (total < 256) {");
    try builder.writeLine("                    all_peers[total] = self.buckets[bi].peers[pi];");
    try builder.writeLine("                    total += 1;");
    try builder.writeLine("                }");
    try builder.writeLine("            }");
    try builder.writeLine("        }");
    try builder.writeLine("        // Sort by XOR distance to target");
    try builder.writeLine("        const slice = all_peers[0..total];");
    try builder.writeLine("        std.mem.sortUnstable(DhtPeer, slice, target, distLessThan);");
    try builder.writeLine("        var result: ClosestResult = undefined;");
    try builder.writeLine("        const n = if (k < total) k else @as(u8, @intCast(total));");
    try builder.writeLine("        for (0..n) |i| {");
    try builder.writeLine("            result.peers[i] = slice[i];");
    try builder.writeLine("        }");
    try builder.writeLine("        result.count = n;");
    try builder.writeLine("        return result;");
    try builder.writeLine("    }");
    try builder.writeLine("};");
    try builder.writeLine("");
}

/// Emit Live Swarm struct
