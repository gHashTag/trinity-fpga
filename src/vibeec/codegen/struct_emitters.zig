    try builder.writeLine("");
    try builder.writeLine("    pub fn healthReport(self: *const SwarmEngine) HealthReport {");
    try builder.writeLine("        var report: HealthReport = .{");
    try builder.writeLine("            .total_nodes = self.node_count,");
    try builder.writeLine("            .nodes_active = 0, .nodes_joining = 0,");
