/// Emit ShardNetwork struct (TCP Transfer Protocol)
pub fn emitShardNetwork(builder: *CodeBuilder) !void {
    try builder.writeLine("");
    try builder.writeLine("// ═══════════════════════════════════════════════════════════════════");
    try builder.writeLine("// SHARD NETWORK — TCP Transfer Protocol (generated from .vibee)");
    try builder.writeLine("// Wire protocol: [64 bytes hex hash][4 bytes data len LE u32][data]");
    try builder.writeLine("// ═══════════════════════════════════════════════════════════════════");
    try builder.writeLine("");
    try builder.writeLine("pub const ShardNetwork = struct {");
    try builder.writeLine("    root_buf: [256]u8,");
    try builder.writeLine("    root_len: usize,");
    try builder.writeLine("    port: u16,");
    try builder.writeLine("");
    try builder.writeLine("    const hex_chars = \"0123456789abcdef\";");
    try builder.writeLine("");
    try builder.writeLine("    /// Create network node with storage directories");
    try builder.writeLine("    pub fn init(root: []const u8, port: u16) !ShardNetwork {");
    try builder.writeLine("        var node = ShardNetwork{");
    try builder.writeLine("            .root_buf = undefined,");
    try builder.writeLine("            .root_len = root.len,");
    try builder.writeLine("            .port = port,");
    try builder.writeLine("        };");
    try builder.writeLine("        @memcpy(node.root_buf[0..root.len], root);");
    try builder.writeLine("        std.fs.makeDirAbsolute(root) catch |e| switch (e) {");
    try builder.writeLine("            error.PathAlreadyExists => {},");
    try builder.writeLine("            else => return e,");
    try builder.writeLine("        };");
    try builder.writeLine("        var sbuf: [280]u8 = undefined;");
    try builder.writeLine("        const sdir = std.fmt.bufPrint(&sbuf, \"{s}/shards\", .{root}) catch unreachable;");
    try builder.writeLine("        std.fs.makeDirAbsolute(sdir) catch |e| switch (e) {");
    try builder.writeLine("            error.PathAlreadyExists => {},");
    try builder.writeLine("            else => return e,");
    try builder.writeLine("        };");
    try builder.writeLine("        return node;");
    try builder.writeLine("    }");
    try builder.writeLine("");
    try builder.writeLine("    fn rootPath(self: *const ShardNetwork) []const u8 {");
    try builder.writeLine("        return self.root_buf[0..self.root_len];");
    try builder.writeLine("    }");
    try builder.writeLine("");
    try builder.writeLine("    fn hashToHex(hash: [32]u8) [64]u8 {");
    try builder.writeLine("        var result: [64]u8 = undefined;");
    try builder.writeLine("        for (hash, 0..) |byte, i| {");
    try builder.writeLine("            result[i * 2] = hex_chars[byte >> 4];");
    try builder.writeLine("            result[i * 2 + 1] = hex_chars[byte & 0x0F];");
    try builder.writeLine("        }");
    try builder.writeLine("        return result;");
    try builder.writeLine("    }");
    try builder.writeLine("");
    try builder.writeLine("    /// Bind TCP listener on port (use port 0 for OS-assigned)");
    try builder.writeLine("    pub fn listen(self: *const ShardNetwork) !std.net.Server {");
    try builder.writeLine("        const addr = std.net.Address.initIp4([4]u8{ 127, 0, 0, 1 }, self.port);");
    try builder.writeLine("        return addr.listen(.{ .reuse_address = true });");
    try builder.writeLine("    }");
    try builder.writeLine("");
    try builder.writeLine("    /// Accept one connection, read protocol, store shard to disk");
    try builder.writeLine("    pub fn receiveOne(self: *const ShardNetwork, server: *std.net.Server) !void {");
    try builder.writeLine("        const conn = try server.accept();");
    try builder.writeLine("        defer conn.stream.close();");
    try builder.writeLine("        var hash_buf: [64]u8 = undefined;");
    try builder.writeLine("        const hn = try conn.stream.readAtLeast(&hash_buf, 64);");
    try builder.writeLine("        if (hn != 64) return error.ProtocolError;");
    try builder.writeLine("        var len_buf: [4]u8 = undefined;");
    try builder.writeLine("        const ln = try conn.stream.readAtLeast(&len_buf, 4);");
    try builder.writeLine("        if (ln != 4) return error.ProtocolError;");
    try builder.writeLine("        const data_len = std.mem.readInt(u32, &len_buf, .little);");
    try builder.writeLine("        var data_buf: [8192]u8 = undefined;");
    try builder.writeLine("        const dn = try conn.stream.readAtLeast(data_buf[0..data_len], data_len);");
    try builder.writeLine("        if (dn != data_len) return error.ProtocolError;");
    try builder.writeLine("        var pbuf: [350]u8 = undefined;");
    try builder.writeLine("        const spath = std.fmt.bufPrint(&pbuf, \"{s}/shards/{s}.shard\", .{ self.rootPath(), hash_buf }) catch unreachable;");
    try builder.writeLine("        const file = try std.fs.createFileAbsolute(spath, .{});");
    try builder.writeLine("        defer file.close();");
    try builder.writeLine("        try file.writeAll(data_buf[0..dn]);");
    try builder.writeLine("    }");
    try builder.writeLine("");
    try builder.writeLine("    /// Connect to peer and send shard via TCP wire protocol");
    try builder.writeLine("    pub fn sendShard(_: *const ShardNetwork, peer_port: u16, hex: *const [64]u8, data: []const u8) !void {");
    try builder.writeLine("        const addr = std.net.Address.initIp4([4]u8{ 127, 0, 0, 1 }, peer_port);");
    try builder.writeLine("        const stream = try std.net.tcpConnectToAddress(addr);");
    try builder.writeLine("        defer stream.close();");
    try builder.writeLine("        stream.writeAll(hex) catch return error.SendFailed;");
    try builder.writeLine("        var len_buf: [4]u8 = undefined;");
    try builder.writeLine("        std.mem.writeInt(u32, &len_buf, @intCast(data.len), .little);");
    try builder.writeLine("        stream.writeAll(&len_buf) catch return error.SendFailed;");
    try builder.writeLine("        stream.writeAll(data) catch return error.SendFailed;");
    try builder.writeLine("    }");
    try builder.writeLine("");
    try builder.writeLine("    /// Remove all storage (for testing)");
    try builder.writeLine("    pub fn cleanup(self: *const ShardNetwork) void {");
    try builder.writeLine("        std.fs.deleteTreeAbsolute(self.rootPath()) catch {};");
    try builder.writeLine("    }");
    try builder.writeLine("};");
    try builder.writeLine("");
}

/// Emit ReedSolomon struct (GF(2^8) Erasure Coding)
pub fn emitReedSolomon(builder: *CodeBuilder) !void {
    try builder.writeLine("");
    try builder.writeLine("// ═══════════════════════════════════════════════════════════════════");
    try builder.writeLine("// REED-SOLOMON ERASURE CODING — GF(2^8) Fault Tolerance");
    try builder.writeLine("// Primitive polynomial: x^8 + x^4 + x^3 + x^2 + 1 (0x11D)");
    try builder.writeLine("// Vandermonde matrix encoding, Gaussian elimination decoding.");
    try builder.writeLine("// ═══════════════════════════════════════════════════════════════════");
    try builder.writeLine("");
    try builder.writeLine("pub const ReedSolomon = struct {");
    try builder.writeLine("    data_shards: u8,");
    try builder.writeLine("    total_shards: u8,");
    try builder.writeLine("");
    try builder.writeLine("    pub fn init(k: u8, m: u8) ReedSolomon {");
    try builder.writeLine("        return .{ .data_shards = k, .total_shards = k + m };");
    try builder.writeLine("    }");
    try builder.writeLine("");
    try builder.writeLine("    /// GF(2^8) multiply via Russian peasant algorithm");
    try builder.writeLine("    pub fn gfMul(a_in: u8, b_in: u8) u8 {");
    try builder.writeLine("        if (a_in == 0 or b_in == 0) return 0;");
    try builder.writeLine("        var a: u16 = a_in;");
    try builder.writeLine("        var b: u8 = b_in;");
    try builder.writeLine("        var p: u8 = 0;");
    try builder.writeLine("        var i: u8 = 0;");
    try builder.writeLine("        while (i < 8) : (i += 1) {");
    try builder.writeLine("            if (b & 1 != 0) p ^= @intCast(a & 0xFF);");
    try builder.writeLine("            a <<= 1;");
    try builder.writeLine("            if (a & 0x100 != 0) a ^= 0x11D;");
    try builder.writeLine("            b >>= 1;");
    try builder.writeLine("        }");
    try builder.writeLine("        return p;");
    try builder.writeLine("    }");
    try builder.writeLine("");
    try builder.writeLine("    /// GF(2^8) exponentiation via repeated squaring");
    try builder.writeLine("    pub fn gfPow(base: u8, exp: u8) u8 {");
    try builder.writeLine("        if (exp == 0) return 1;");
    try builder.writeLine("        if (base == 0) return 0;");
    try builder.writeLine("        var result: u8 = 1;");
    try builder.writeLine("        var b: u8 = base;");
    try builder.writeLine("        var e: u8 = exp;");
    try builder.writeLine("        while (e > 0) {");
    try builder.writeLine("            if (e & 1 != 0) result = gfMul(result, b);");
    try builder.writeLine("            b = gfMul(b, b);");
    try builder.writeLine("            e >>= 1;");
    try builder.writeLine("        }");
    try builder.writeLine("        return result;");
    try builder.writeLine("    }");
    try builder.writeLine("");
    try builder.writeLine("    /// GF(2^8) inverse: a^(-1) = a^254 (Fermat's little theorem)");
    try builder.writeLine("    pub fn gfInv(a: u8) u8 {");
    try builder.writeLine("        if (a == 0) return 0;");
    try builder.writeLine("        return gfPow(a, 254);");
    try builder.writeLine("    }");
    try builder.writeLine("");
    try builder.writeLine("    /// Encode one byte position: k input bytes → n coded bytes (Vandermonde)");
    try builder.writeLine("    pub fn encodeByte(self: *const ReedSolomon, input: []const u8, output: []u8) void {");
    try builder.writeLine("        var i: u8 = 0;");
    try builder.writeLine("        while (i < self.total_shards) : (i += 1) {");
    try builder.writeLine("            var val: u8 = 0;");
    try builder.writeLine("            var j: u8 = 0;");
    try builder.writeLine("            while (j < self.data_shards) : (j += 1) {");
    try builder.writeLine("                const coeff = gfPow(i + 1, j);");
    try builder.writeLine("                val ^= gfMul(coeff, input[j]);");
    try builder.writeLine("            }");
    try builder.writeLine("            output[i] = val;");
    try builder.writeLine("        }");
    try builder.writeLine("    }");
    try builder.writeLine("");
    try builder.writeLine("    /// Decode one byte position: any k of n coded bytes → k original bytes");
    try builder.writeLine("    /// avail = k available bytes, indices = their shard indices (0-based)");
    try builder.writeLine("    pub fn decodeByte(self: *const ReedSolomon, avail: []const u8, indices: []const u8, output: []u8) !void {");
    try builder.writeLine("        const k = self.data_shards;");
    try builder.writeLine("        var mat: [8][8]u8 = undefined;");
    try builder.writeLine("        var aug: [8][8]u8 = undefined;");
    try builder.writeLine("        var r: usize = 0;");
    try builder.writeLine("        while (r < k) : (r += 1) {");
    try builder.writeLine("            var c: usize = 0;");
    try builder.writeLine("            while (c < k) : (c += 1) {");
    try builder.writeLine("                mat[r][c] = gfPow(indices[r] + 1, @intCast(c));");
    try builder.writeLine("                aug[r][c] = if (r == c) 1 else 0;");
    try builder.writeLine("            }");
    try builder.writeLine("        }");
    try builder.writeLine("        var col: usize = 0;");
    try builder.writeLine("        while (col < k) : (col += 1) {");
    try builder.writeLine("            if (mat[col][col] == 0) {");
    try builder.writeLine("                var sr: usize = col + 1;");
    try builder.writeLine("                while (sr < k) : (sr += 1) {");
    try builder.writeLine("                    if (mat[sr][col] != 0) {");
    try builder.writeLine("                        var sc: usize = 0;");
    try builder.writeLine("                        while (sc < k) : (sc += 1) {");
    try builder.writeLine("                            const tmp1 = mat[col][sc]; mat[col][sc] = mat[sr][sc]; mat[sr][sc] = tmp1;");
    try builder.writeLine("                            const tmp2 = aug[col][sc]; aug[col][sc] = aug[sr][sc]; aug[sr][sc] = tmp2;");
    try builder.writeLine("                        }");
    try builder.writeLine("                        break;");
    try builder.writeLine("                    }");
    try builder.writeLine("                }");
    try builder.writeLine("            }");
    try builder.writeLine("            const piv_inv = gfInv(mat[col][col]);");
    try builder.writeLine("            var sc2: usize = 0;");
    try builder.writeLine("            while (sc2 < k) : (sc2 += 1) {");
    try builder.writeLine("                mat[col][sc2] = gfMul(mat[col][sc2], piv_inv);");
    try builder.writeLine("                aug[col][sc2] = gfMul(aug[col][sc2], piv_inv);");
    try builder.writeLine("            }");
    try builder.writeLine("            var er: usize = 0;");
    try builder.writeLine("            while (er < k) : (er += 1) {");
    try builder.writeLine("                if (er == col) { er += 0; } else {");
    try builder.writeLine("                    const factor = mat[er][col];");
    try builder.writeLine("                    if (factor != 0) {");
    try builder.writeLine("                        var ec: usize = 0;");
    try builder.writeLine("                        while (ec < k) : (ec += 1) {");
    try builder.writeLine("                            mat[er][ec] ^= gfMul(factor, mat[col][ec]);");
    try builder.writeLine("                            aug[er][ec] ^= gfMul(factor, aug[col][ec]);");
    try builder.writeLine("                        }");
    try builder.writeLine("                    }");
    try builder.writeLine("                }");
    try builder.writeLine("            }");
    try builder.writeLine("        }");
    try builder.writeLine("        var oi: usize = 0;");
    try builder.writeLine("        while (oi < k) : (oi += 1) {");
    try builder.writeLine("            var val: u8 = 0;");
    try builder.writeLine("            var oj: usize = 0;");
    try builder.writeLine("            while (oj < k) : (oj += 1) {");
    try builder.writeLine("                val ^= gfMul(aug[oi][oj], avail[oj]);");
    try builder.writeLine("            }");
    try builder.writeLine("            output[oi] = val;");
    try builder.writeLine("        }");
    try builder.writeLine("    }");
    try builder.writeLine("};");
    try builder.writeLine("");
}

/// Emit PeerRegistry + ShardManifest structs
pub fn emitDiscovery(builder: *CodeBuilder) !void {
    try builder.writeLine("");
    try builder.writeLine("// ═══════════════════════════════════════════════════════════════════");
    try builder.writeLine("// PEER DISCOVERY + SELF-HEALING — Dynamic Swarm Recovery");
    try builder.writeLine("// PeerRegistry: in-memory peer table with alive/dead status.");
    try builder.writeLine("// ShardManifest: maps data groups → (shard_index, peer_id) pairs.");
    try builder.writeLine("// ═══════════════════════════════════════════════════════════════════");
    try builder.writeLine("");
    try builder.writeLine("pub const PeerRegistry = struct {");
    try builder.writeLine("    const MAX_PEERS = 8;");
    try builder.writeLine("");
    try builder.writeLine("    ports: [MAX_PEERS]u16,");
    try builder.writeLine("    alive: [MAX_PEERS]bool,");
    try builder.writeLine("    shard_counts: [MAX_PEERS]u16,");
    try builder.writeLine("    count: u8,");
    try builder.writeLine("");
    try builder.writeLine("    pub fn init() PeerRegistry {");
    try builder.writeLine("        return .{");
    try builder.writeLine("            .ports = [_]u16{0} ** MAX_PEERS,");
    try builder.writeLine("            .alive = [_]bool{false} ** MAX_PEERS,");
    try builder.writeLine("            .shard_counts = [_]u16{0} ** MAX_PEERS,");
    try builder.writeLine("            .count = 0,");
    try builder.writeLine("        };");
    try builder.writeLine("    }");
    try builder.writeLine("");
    try builder.writeLine("    /// Register a new peer, returns peer_id (index)");
    try builder.writeLine("    pub fn registerPeer(self: *PeerRegistry, port: u16) !u8 {");
    try builder.writeLine("        if (self.count >= MAX_PEERS) return error.RegistryFull;");
    try builder.writeLine("        const id = self.count;");
    try builder.writeLine("        self.ports[id] = port;");
    try builder.writeLine("        self.alive[id] = true;");
    try builder.writeLine("        self.shard_counts[id] = 0;");
    try builder.writeLine("        self.count += 1;");
    try builder.writeLine("        return id;");
    try builder.writeLine("    }");
    try builder.writeLine("");
    try builder.writeLine("    /// Mark a peer as dead (failed)");
    try builder.writeLine("    pub fn markDead(self: *PeerRegistry, peer_id: u8) void {");
    try builder.writeLine("        if (peer_id < self.count) self.alive[peer_id] = false;");
    try builder.writeLine("    }");
    try builder.writeLine("");
    try builder.writeLine("    /// Check if peer is alive");
    try builder.writeLine("    pub fn isAlive(self: *const PeerRegistry, peer_id: u8) bool {");
    try builder.writeLine("        if (peer_id >= self.count) return false;");
    try builder.writeLine("        return self.alive[peer_id];");
    try builder.writeLine("    }");
    try builder.writeLine("");
    try builder.writeLine("    /// Count alive peers");
    try builder.writeLine("    pub fn alivePeers(self: *const PeerRegistry) u8 {");
    try builder.writeLine("        var c: u8 = 0;");
    try builder.writeLine("        var i: u8 = 0;");
    try builder.writeLine("        while (i < self.count) : (i += 1) {");
    try builder.writeLine("            if (self.alive[i]) c += 1;");
    try builder.writeLine("        }");
    try builder.writeLine("        return c;");
    try builder.writeLine("    }");
    try builder.writeLine("");
    try builder.writeLine("    /// Get port for a peer");
    try builder.writeLine("    pub fn getPort(self: *const PeerRegistry, peer_id: u8) u16 {");
    try builder.writeLine("        return self.ports[peer_id];");
    try builder.writeLine("    }");
    try builder.writeLine("");
    try builder.writeLine("    /// Increment shard count for a peer");
    try builder.writeLine("    pub fn incShards(self: *PeerRegistry, peer_id: u8) void {");
    try builder.writeLine("        if (peer_id < self.count) self.shard_counts[peer_id] += 1;");
    try builder.writeLine("    }");
    try builder.writeLine("};");
    try builder.writeLine("");
    try builder.writeLine("pub const ShardManifest = struct {");
    try builder.writeLine("    const MAX_GROUPS = 16;");
    try builder.writeLine("    const MAX_ENTRIES = 8;");
    try builder.writeLine("");
    try builder.writeLine("    /// Each entry: (shard_index, peer_id)");
    try builder.writeLine("    shard_idx: [MAX_GROUPS][MAX_ENTRIES]u8,");
    try builder.writeLine("    peer_ids: [MAX_GROUPS][MAX_ENTRIES]u8,");
    try builder.writeLine("    entry_counts: [MAX_GROUPS]u8,");
    try builder.writeLine("    group_count: u8,");
    try builder.writeLine("");
    try builder.writeLine("    pub fn init() ShardManifest {");
    try builder.writeLine("        return .{");
    try builder.writeLine("            .shard_idx = [_][MAX_ENTRIES]u8{[_]u8{0} ** MAX_ENTRIES} ** MAX_GROUPS,");
    try builder.writeLine("            .peer_ids = [_][MAX_ENTRIES]u8{[_]u8{0} ** MAX_ENTRIES} ** MAX_GROUPS,");
    try builder.writeLine("            .entry_counts = [_]u8{0} ** MAX_GROUPS,");
    try builder.writeLine("            .group_count = 0,");
    try builder.writeLine("        };");
    try builder.writeLine("    }");
    try builder.writeLine("");
    try builder.writeLine("    /// Record that shard_index of data group is held by peer_id");
    try builder.writeLine("    pub fn recordShard(self: *ShardManifest, group: u8, shard_index: u8, peer_id: u8) void {");
    try builder.writeLine("        if (group >= MAX_GROUPS) return;");
    try builder.writeLine("        const ec = self.entry_counts[group];");
    try builder.writeLine("        if (ec >= MAX_ENTRIES) return;");
    try builder.writeLine("        self.shard_idx[group][ec] = shard_index;");
    try builder.writeLine("        self.peer_ids[group][ec] = peer_id;");
    try builder.writeLine("        self.entry_counts[group] = ec + 1;");
    try builder.writeLine("        if (group >= self.group_count) self.group_count = group + 1;");
    try builder.writeLine("    }");
    try builder.writeLine("");
    try builder.writeLine("    /// Query surviving shards for a group: returns count of alive entries");
    try builder.writeLine("    /// Writes surviving shard indices to out_shard_idx and peer ids to out_peer_ids");
    try builder.writeLine("    pub fn survivorsForGroup(self: *const ShardManifest, group: u8, registry: *const PeerRegistry, out_shard_idx: []u8, out_peer_ids: []u8) u8 {");
    try builder.writeLine("        if (group >= MAX_GROUPS) return 0;");
    try builder.writeLine("        var sc: u8 = 0;");
    try builder.writeLine("        var i: u8 = 0;");
    try builder.writeLine("        while (i < self.entry_counts[group]) : (i += 1) {");
    try builder.writeLine("            if (registry.isAlive(self.peer_ids[group][i])) {");
    try builder.writeLine("                if (sc < out_shard_idx.len) {");
    try builder.writeLine("                    out_shard_idx[sc] = self.shard_idx[group][i];");
    try builder.writeLine("                    out_peer_ids[sc] = self.peer_ids[group][i];");
    try builder.writeLine("                    sc += 1;");
    try builder.writeLine("                }");
    try builder.writeLine("            }");
    try builder.writeLine("        }");
    try builder.writeLine("        return sc;");
    try builder.writeLine("    }");
    try builder.writeLine("};");
    try builder.writeLine("");
}

