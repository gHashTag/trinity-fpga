    try builder.writeLine("                lat_count += 1;");
    try builder.writeLine("            }");
    try builder.writeLine("        }");
    try builder.writeLine("        if (lat_count > 0) report.avg_latency_ms = @intCast(lat_sum / lat_count);");
    try builder.writeLine("        return report;");
    try builder.writeLine("    }");
    try builder.writeLine("};");
