// ============================================================================
// VSA φ-BINDING — Zero-DSP48 Test (Simplified)
// ============================================================================
//
// Demonstrates φ-rotation using ONLY ADDERS
//   rotated = vector + φ × angle
//
// Key: φ × angle = angle + angle_prev (Fibonacci identity!)
//   → ONE ADDER instead of ONE DSP48
//
// For 1024-dim hypervectors:
//   Standard: 1024 DSP48 (impossible, only 240 available!)
//   φ-method:  0 DSP48 + 2048 adders (fits easily!)
//
// Generated by TRI sacred-const vsa-bind
// Verilog-2005 compatible
//
// ============================================================================

`default_nettype none

module vsa_phi_simple_top (
    input  wire clk,
    input  wire rst,
    input  wire [24:0] vector_in,
    input  wire [24:0] angle,
    output wire [24:0] rotated_out,
    output wire led     // For hardware verification
);

    // φ × angle = angle + angle_prev
    reg [24:0] angle_prev;

    always @(posedge clk) begin
        if (rst) begin
            angle_prev <= 25'd0;
        end else begin
            angle_prev <= angle;  // Store previous value
        end
    end

    // rotated = vector + (angle + angle_prev) = vector + φ × angle
    wire [24:0] phi_angle = angle + angle_prev;
    wire [24:0] rotated = vector_in + phi_angle;

    assign rotated_out = rotated;

    // LED blinks to show FPGA is working
    reg [23:0] led_counter;
    reg led_state;

    always @(posedge clk) begin
        led_counter <= led_counter + 1;
        if (led_counter == 24'd25000000) begin  // ~1 Hz at 50 MHz
            led_counter <= 24'd0;
            led_state <= ~led_state;
        end
    end

    // ACTIVE-LOW LED: invert!
    assign led = ~led_state;

endmodule

`default_nettype wire
